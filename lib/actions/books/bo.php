<h4 class="centered">Dewey Shireman's Book of Creatures: The Bo</h4>
<p>Bos are large, flightless birds, related to Earth's Ostrich.  They travel in packs across the great plains of the Hollow Earth, feeding mostly on fruits and vegetables and occasionally scavenging meat.</p>
<h5><img src="gfx/pets/vanillabo.png" align="left" alt="" />Vanilla Bo</h5>
<p>The Vanilla Bo is by far the most common variety of Bo.  Other colors are very unusual, but do exist.</p>
<h5><img src="gfx/pets/applebo.png" align="left" alt="" />Apple Bo</h5>
<p>Apple Bos are only slightly redder than the common Vanilla Bo.  Though the difference is only slight, it is quite rare.</p>
<h5><img src="gfx/pets/blueberrybo.png" align="left" alt="" />Blueberry Bo</h5>
<p>The Blueberry Bo has a distinctive blue color that is the result of a rare genetic mutation.  In the wild these off-color Bos are usually abandoned as babies by the herd and quickly devoured by predators.</p>
<h5><img src="gfx/pets/limebo.png" align="left" alt="" />Lime Bo</h5>
<p>Like Blueberry Bos, Lime Bos are almost always ostracized by the heard.  Statistically they appear to have a higher survivability rate than the Blueberry Bo, however the reasons for this are not yet known.</p>
<p>Though these are the only naturally occuring Bos known, others have been made thanks to genetic engineering and cloning technologies.  Some wealthy PsyPets residents have even commissioned a few such Bos.</p>
