SCENE IV. The Queen's closet.<br>
<br>
<i>Enter QUEEN MARGARET and POLONIUS</i><br>
LORD POLONIUS <br>
He will come straight. Look you lay home to him:<br>
Tell him his pranks have been too broad to bear with,<br>
And that your grace hath screen'd and stood between<br>
Much heat and him. I'll sconce me even here.<br>
Pray you, be round with him.<br>
<br>
HAMLET <br>
[Within] Mother, mother, mother!<br>
<br>
QUEEN GERTRUDE <br>
I'll warrant you,<br>
Fear me not: withdraw, I hear him coming.<br>
<br>
<i>POLONIUS hides behind the arras</i><br>
<br>
<i>Enter HAMLET</i><br>
<br>
HAMLET <br>
Now, mother, what's the matter?<br>
<br>
QUEEN GERTRUDE <br>
Hamlet, thou hast thy father much offended.<br>
<br>
HAMLET <br>
Mother, you have my father much offended.<br>
<br>
QUEEN GERTRUDE <br>
Come, come, you answer with an idle tongue.<br>
<br>
HAMLET <br>
Go, go, you question with a wicked tongue.<br>
<br>
QUEEN GERTRUDE <br>
Why, how now, Hamlet!<br>
<br>
HAMLET <br>
What's the matter now?<br>
<br>
QUEEN GERTRUDE <br>
Have you forgot me?<br>
<br>
HAMLET <br>
No, by the rood, not so:<br>
You are the queen, your husband's brother's wife;<br>
And--would it were not so!--you are my mother.<br>
<br>
QUEEN GERTRUDE <br>
Nay, then, I'll set those to you that can speak.<br>
<br>
HAMLET <br>
Come, come, and sit you down; you shall not budge;<br>
You go not till I set you up a glass<br>
Where you may see the inmost part of you.<br>
<br>
QUEEN GERTRUDE <br>
What wilt thou do? thou wilt not murder me?<br>
Help, help, ho!<br>
<br>
LORD POLONIUS <br>
[Behind] What, ho! help, help, help!<br>
<br>
HAMLET <br>
[Drawing] How now! a rat? Dead, for a ducat, dead!<br>
<br>
<i>Makes a pass through the arras</i><br>
<br>
LORD POLONIUS <br>
[Behind] O, I am slain!<br>
<br>
<i>Falls and dies</i><br>
<br>
QUEEN GERTRUDE <br>
O me, what hast thou done?<br>
<br>
HAMLET <br>
Nay, I know not:<br>
Is it the king?<br>
<br>
QUEEN GERTRUDE <br>
O, what a rash and bloody deed is this!<br>
<br>
HAMLET <br>
A bloody deed! almost as bad, good mother,<br>
As kill a king, and marry with his brother.<br>
<br>
QUEEN GERTRUDE <br>
As kill a king!<br>
<br>
HAMLET <br>
Ay, lady, 'twas my word.<br>
<br>
<i>Lifts up the array and discovers POLONIUS</i><br>
<br>
Thou wretched, rash, intruding fool, farewell!<br>
I took thee for thy better: take thy fortune;<br>
Thou find'st to be too busy is some danger.<br>
Leave wringing of your hands: peace! sit you down,<br>
And let me wring your heart; for so I shall,<br>
If it be made of penetrable stuff,<br>
If damned custom have not brass'd it so<br>
That it is proof and bulwark against sense.<br>
<br>
QUEEN GERTRUDE <br>
What have I done, that thou darest wag thy tongue<br>
In noise so rude against me?<br>
<br>
HAMLET <br>
Such an act<br>
That blurs the grace and blush of modesty,<br>
Calls virtue hypocrite, takes off the rose<br>
From the fair forehead of an innocent love<br>
And sets a blister there, makes marriage-vows<br>
As false as dicers' oaths: O, such a deed<br>
As from the body of contraction plucks<br>
The very soul, and sweet religion makes<br>
A rhapsody of words: heaven's face doth glow:<br>
Yea, this solidity and compound mass,<br>
With tristful visage, as against the doom,<br>
Is thought-sick at the act.<br>
<br>
QUEEN GERTRUDE <br>
Ay me, what act,<br>
That roars so loud, and thunders in the index?<br>
<br>
HAMLET <br>
Look here, upon this picture, and on this,<br>
The counterfeit presentment of two brothers.<br>
See, what a grace was seated on this brow;<br>
Hyperion's curls; the front of Jove himself;<br>
An eye like Mars, to threaten and command;<br>
A station like the herald Mercury<br>
New-lighted on a heaven-kissing hill;<br>
A combination and a form indeed,<br>
Where every god did seem to set his seal,<br>
To give the world assurance of a man:<br>
This was your husband. Look you now, what follows:<br>
Here is your husband; like a mildew'd ear,<br>
Blasting his wholesome brother. Have you eyes?<br>
Could you on this fair mountain leave to feed,<br>
And batten on this moor? Ha! have you eyes?<br>
You cannot call it love; for at your age<br>
The hey-day in the blood is tame, it's humble,<br>
And waits upon the judgment: and what judgment<br>
Would step from this to this? Sense, sure, you have,<br>
Else could you not have motion; but sure, that sense<br>
Is apoplex'd; for madness would not err,<br>
Nor sense to ecstasy was ne'er so thrall'd<br>
But it reserved some quantity of choice,<br>
To serve in such a difference. What devil was't<br>
That thus hath cozen'd you at hoodman-blind?<br>
Eyes without feeling, feeling without sight,<br>
Ears without hands or eyes, smelling sans all,<br>
Or but a sickly part of one true sense<br>
Could not so mope.<br>
O shame! where is thy blush? Rebellious hell,<br>
If thou canst mutine in a matron's bones,<br>
To flaming youth let virtue be as wax,<br>
And melt in her own fire: proclaim no shame<br>
When the compulsive ardour gives the charge,<br>
Since frost itself as actively doth burn<br>
And reason panders will.<br>
<br>
QUEEN GERTRUDE <br>
O Hamlet, speak no more:<br>
Thou turn'st mine eyes into my very soul;<br>
And there I see such black and grained spots<br>
As will not leave their tinct.<br>
<br>
HAMLET <br>
Nay, but to live<br>
In the rank sweat of an enseamed bed,<br>
Stew'd in corruption, honeying and making love<br>
Over the nasty sty,--<br>
<br>
QUEEN GERTRUDE <br>
O, speak to me no more;<br>
These words, like daggers, enter in mine ears;<br>
No more, sweet Hamlet!<br>
<br>
HAMLET <br>
A murderer and a villain;<br>
A slave that is not twentieth part the tithe<br>
Of your precedent lord; a vice of kings;<br>
A cutpurse of the empire and the rule,<br>
That from a shelf the precious diadem stole,<br>
And put it in his pocket!<br>
<br>
QUEEN GERTRUDE <br>
No more!<br>
<br>
HAMLET <br>
A king of shreds and patches,--<br>
<br>
<i>Enter Ghost</i><br>
<br>
Save me, and hover o'er me with your wings,<br>
You heavenly guards! What would your gracious figure?<br>
<br>
QUEEN GERTRUDE <br>
Alas, he's mad!<br>
<br>
HAMLET <br>
Do you not come your tardy son to chide,<br>
That, lapsed in time and passion, lets go by<br>
The important acting of your dread command? O, say!<br>
<br>
Ghost <br>
Do not forget: this visitation<br>
Is but to whet thy almost blunted purpose.<br>
But, look, amazement on thy mother sits:<br>
O, step between her and her fighting soul:<br>
Conceit in weakest bodies strongest works:<br>
Speak to her, Hamlet.<br>
<br>
HAMLET <br>
How is it with you, lady?<br>
<br>
QUEEN GERTRUDE <br>
Alas, how is't with you,<br>
That you do bend your eye on vacancy<br>
And with the incorporal air do hold discourse?<br>
Forth at your eyes your spirits wildly peep;<br>
And, as the sleeping soldiers in the alarm,<br>
Your bedded hair, like life in excrements,<br>
Starts up, and stands on end. O gentle son,<br>
Upon the heat and flame of thy distemper<br>
Sprinkle cool patience. Whereon do you look?<br>
<br>
HAMLET <br>
On him, on him! Look you, how pale he glares!<br>
His form and cause conjoin'd, preaching to stones,<br>
Would make them capable. Do not look upon me;<br>
Lest with this piteous action you convert<br>
My stern effects: then what I have to do<br>
Will want true colour; tears perchance for blood.<br>
<br>
QUEEN GERTRUDE <br>
To whom do you speak this?<br>
<br>
HAMLET <br>
Do you see nothing there?<br>
<br>
QUEEN GERTRUDE <br>
Nothing at all; yet all that is I see.<br>
<br>
HAMLET <br>
Nor did you nothing hear?<br>
<br>
QUEEN GERTRUDE <br>
No, nothing but ourselves.<br>
<br>
HAMLET <br>
Why, look you there! look, how it steals away!<br>
My father, in his habit as he lived!<br>
Look, where he goes, even now, out at the portal!<br>
<br>
<i>Exit Ghost</i><br>
<br>
QUEEN GERTRUDE <br>
This the very coinage of your brain:<br>
This bodiless creation ecstasy<br>
Is very cunning in.<br>
<br>
HAMLET <br>
Ecstasy!<br>
My pulse, as yours, doth temperately keep time,<br>
And makes as healthful music: it is not madness<br>
That I have utter'd: bring me to the test,<br>
And I the matter will re-word; which madness<br>
Would gambol from. Mother, for love of grace,<br>
Lay not that mattering unction to your soul,<br>
That not your trespass, but my madness speaks:<br>
It will but skin and film the ulcerous place,<br>
Whilst rank corruption, mining all within,<br>
Infects unseen. Confess yourself to heaven;<br>
Repent what's past; avoid what is to come;<br>
And do not spread the compost on the weeds,<br>
To make them ranker. Forgive me this my virtue;<br>
For in the fatness of these pursy times<br>
Virtue itself of vice must pardon beg,<br>
Yea, curb and woo for leave to do him good.<br>
<br>
QUEEN GERTRUDE <br>
O Hamlet, thou hast cleft my heart in twain.<br>
<br>
HAMLET <br>
O, throw away the worser part of it,<br>
And live the purer with the other half.<br>
Good night: but go not to mine uncle's bed;<br>
Assume a virtue, if you have it not.<br>
That monster, custom, who all sense doth eat,<br>
Of habits devil, is angel yet in this,<br>
That to the use of actions fair and good<br>
He likewise gives a frock or livery,<br>
That aptly is put on. Refrain to-night,<br>
And that shall lend a kind of easiness<br>
To the next abstinence: the next more easy;<br>
For use almost can change the stamp of nature,<br>
And either [ ] the devil, or throw him out<br>
With wondrous potency. Once more, good night:<br>
And when you are desirous to be bless'd,<br>
I'll blessing beg of you. For this same lord,<br>
<br>
<i>Pointing to POLONIUS</i><br>
<br>
I do repent: but heaven hath pleased it so,<br>
To punish me with this and this with me,<br>
That I must be their scourge and minister.<br>
I will bestow him, and will answer well<br>
The death I gave him. So, again, good night.<br>
I must be cruel, only to be kind:<br>
Thus bad begins and worse remains behind.<br>
One word more, good lady.<br>
<br>
QUEEN GERTRUDE <br>
What shall I do?<br>
<br>
HAMLET <br>
Not this, by no means, that I bid you do:<br>
Let the bloat king tempt you again to bed;<br>
Pinch wanton on your cheek; call you his mouse;<br>
And let him, for a pair of reechy kisses,<br>
Or paddling in your neck with his damn'd fingers,<br>
Make you to ravel all this matter out,<br>
That I essentially am not in madness,<br>
But mad in craft. 'Twere good you let him know;<br>
For who, that's but a queen, fair, sober, wise,<br>
Would from a paddock, from a bat, a gib,<br>
Such dear concernings hide? who would do so?<br>
No, in despite of sense and secrecy,<br>
Unpeg the basket on the house's top.<br>
Let the birds fly, and, like the famous ape,<br>
To try conclusions, in the basket creep,<br>
And break your own neck down.<br>
<br>
QUEEN GERTRUDE <br>
Be thou assured, if words be made of breath,<br>
And breath of life, I have no life to breathe<br>
What thou hast said to me.<br>
<br>
HAMLET <br>
I must to England; you know that?<br>
<br>
QUEEN GERTRUDE <br>
Alack,<br>
I had forgot: 'tis so concluded on.<br>
<br>
HAMLET <br>
There's letters seal'd: and my two schoolfellows,<br>
Whom I will trust as I will adders fang'd,<br>
They bear the mandate; they must sweep my way,<br>
And marshal me to knavery. Let it work;<br>
For 'tis the sport to have the engineer<br>
Hoist with his own petard: and 't shall go hard<br>
But I will delve one yard below their mines,<br>
And blow them at the moon: O, 'tis most sweet,<br>
When in one line two crafts directly meet.<br>
This man shall set me packing:<br>
I'll lug the guts into the neighbour room.<br>
Mother, good night. Indeed this counsellor<br>
Is now most still, most secret and most grave,<br>
Who was in life a foolish prating knave.<br>
Come, sir, to draw toward an end with you.<br>
Good night, mother.<br>
<br>
<i>Exeunt severally; HAMLET dragging in POLONIUS</i><br>
