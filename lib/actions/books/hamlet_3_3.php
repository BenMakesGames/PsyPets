SCENE III. A room in the castle.<br>
<br>
<i>Enter KING CLAUDIUS, ROSENCRANTZ, and GUILDENSTERN</i><br>
KING CLAUDIUS <br>
I like him not, nor stands it safe with us<br>
To let his madness range. Therefore prepare you;<br>
I your commission will forthwith dispatch,<br>
And he to England shall along with you:<br>
The terms of our estate may not endure<br>
Hazard so dangerous as doth hourly grow<br>
Out of his lunacies.<br>
<br>
GUILDENSTERN <br>
We will ourselves provide:<br>
Most holy and religious fear it is<br>
To keep those many many bodies safe<br>
That live and feed upon your majesty.<br>
<br>
ROSENCRANTZ <br>
The single and peculiar life is bound,<br>
With all the strength and armour of the mind,<br>
To keep itself from noyance; but much more<br>
That spirit upon whose weal depend and rest<br>
The lives of many. The cease of majesty<br>
Dies not alone; but, like a gulf, doth draw<br>
What's near it with it: it is a massy wheel,<br>
Fix'd on the summit of the highest mount,<br>
To whose huge spokes ten thousand lesser things<br>
Are mortised and adjoin'd; which, when it falls,<br>
Each small annexment, petty consequence,<br>
Attends the boisterous ruin. Never alone<br>
Did the king sigh, but with a general groan.<br>
<br>
KING CLAUDIUS <br>
Arm you, I pray you, to this speedy voyage;<br>
For we will fetters put upon this fear,<br>
Which now goes too free-footed.<br>
<br>
ROSENCRANTZ GUILDENSTERN <br>
We will haste us.<br>
<br>
<i>Exeunt ROSENCRANTZ and GUILDENSTERN</i><br>
<br>
<i>Enter POLONIUS</i><br>
<br>
LORD POLONIUS <br>
My lord, he's going to his mother's closet:<br>
Behind the arras I'll convey myself,<br>
To hear the process; and warrant she'll tax him home:<br>
And, as you said, and wisely was it said,<br>
'Tis meet that some more audience than a mother,<br>
Since nature makes them partial, should o'erhear<br>
The speech, of vantage. Fare you well, my liege:<br>
I'll call upon you ere you go to bed,<br>
And tell you what I know.<br>
<br>
KING CLAUDIUS <br>
Thanks, dear my lord.<br>
<br>
<i>Exit POLONIUS</i><br>
<br>
O, my offence is rank it smells to heaven;<br>
It hath the primal eldest curse upon't,<br>
A brother's murder. Pray can I not,<br>
Though inclination be as sharp as will:<br>
My stronger guilt defeats my strong intent;<br>
And, like a man to double business bound,<br>
I stand in pause where I shall first begin,<br>
And both neglect. What if this cursed hand<br>
Were thicker than itself with brother's blood,<br>
Is there not rain enough in the sweet heavens<br>
To wash it white as snow? Whereto serves mercy<br>
But to confront the visage of offence?<br>
And what's in prayer but this two-fold force,<br>
To be forestalled ere we come to fall,<br>
Or pardon'd being down? Then I'll look up;<br>
My fault is past. But, O, what form of prayer<br>
Can serve my turn? 'Forgive me my foul murder'?<br>
That cannot be; since I am still possess'd<br>
Of those effects for which I did the murder,<br>
My crown, mine own ambition and my queen.<br>
May one be pardon'd and retain the offence?<br>
In the corrupted currents of this world<br>
Offence's gilded hand may shove by justice,<br>
And oft 'tis seen the wicked prize itself<br>
Buys out the law: but 'tis not so above;<br>
There is no shuffling, there the action lies<br>
In his true nature; and we ourselves compell'd,<br>
Even to the teeth and forehead of our faults,<br>
To give in evidence. What then? what rests?<br>
Try what repentance can: what can it not?<br>
Yet what can it when one can not repent?<br>
O wretched state! O bosom black as death!<br>
O limed soul, that, struggling to be free,<br>
Art more engaged! Help, angels! Make assay!<br>
Bow, stubborn knees; and, heart with strings of steel,<br>
Be soft as sinews of the newborn babe!<br>
All may be well.<br>
<br>
<i>Retires and kneels</i><br>
<br>
<i>Enter HAMLET</i><br>
<br>
HAMLET <br>
Now might I do it pat, now he is praying;<br>
And now I'll do't. And so he goes to heaven;<br>
And so am I revenged. That would be scann'd:<br>
A villain kills my father; and for that,<br>
I, his sole son, do this same villain send<br>
To heaven.<br>
O, this is hire and salary, not revenge.<br>
He took my father grossly, full of bread;<br>
With all his crimes broad blown, as flush as May;<br>
And how his audit stands who knows save heaven?<br>
But in our circumstance and course of thought,<br>
'Tis heavy with him: and am I then revenged,<br>
To take him in the purging of his soul,<br>
When he is fit and season'd for his passage?<br>
No!<br>
Up, sword; and know thou a more horrid hent:<br>
When he is drunk asleep, or in his rage,<br>
Or in the incestuous pleasure of his bed;<br>
At gaming, swearing, or about some act<br>
That has no relish of salvation in't;<br>
Then trip him, that his heels may kick at heaven,<br>
And that his soul may be as damn'd and black<br>
As hell, whereto it goes. My mother stays:<br>
This physic but prolongs thy sickly days.<br>
<br>
<i>Exit</i><br>
<br>
KING CLAUDIUS <br>
[Rising] My words fly up, my thoughts remain below:<br>
Words without thoughts never to heaven go.<br>
<br>
<i>Exit</i><br>
