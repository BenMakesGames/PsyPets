SCENE I. A room in POLONIUS' house.<br>
<br>
<i>Enter POLONIUS and REYNALDO</i><br>
LORD POLONIUS <br>
Give him this money and these notes, Reynaldo.<br>
<br>
REYNALDO <br>
I will, my lord.<br>
<br>
LORD POLONIUS <br>
You shall do marvellous wisely, good Reynaldo,<br>
Before you visit him, to make inquire<br>
Of his behavior.<br>
<br>
REYNALDO <br>
My lord, I did intend it.<br>
<br>
LORD POLONIUS <br>
Marry, well said; very well said. Look you, sir,<br>
Inquire me first what Danskers are in Paris;<br>
And how, and who, what means, and where they keep,<br>
What company, at what expense; and finding<br>
By this encompassment and drift of question<br>
That they do know my son, come you more nearer<br>
Than your particular demands will touch it:<br>
Take you, as 'twere, some distant knowledge of him;<br>
As thus, 'I know his father and his friends,<br>
And in part him: ' do you mark this, Reynaldo?<br>
<br>
REYNALDO <br>
Ay, very well, my lord.<br>
<br>
LORD POLONIUS <br>
'And in part him; but' you may say 'not well:<br>
But, if't be he I mean, he's very wild;<br>
Addicted so and so:' and there put on him<br>
What forgeries you please; marry, none so rank<br>
As may dishonour him; take heed of that;<br>
But, sir, such wanton, wild and usual slips<br>
As are companions noted and most known<br>
To youth and liberty.<br>
<br>
REYNALDO <br>
As gaming, my lord.<br>
<br>
LORD POLONIUS <br>
Ay, or drinking, fencing, swearing, quarrelling,<br>
Drabbing: you may go so far.<br>
<br>
REYNALDO <br>
My lord, that would dishonour him.<br>
<br>
LORD POLONIUS <br>
'Faith, no; as you may season it in the charge<br>
You must not put another scandal on him,<br>
That he is open to incontinency;<br>
That's not my meaning: but breathe his faults so quaintly<br>
That they may seem the taints of liberty,<br>
The flash and outbreak of a fiery mind,<br>
A savageness in unreclaimed blood,<br>
Of general assault.<br>
<br>
REYNALDO <br>
But, my good lord,--<br>
<br>
LORD POLONIUS <br>
Wherefore should you do this?<br>
<br>
REYNALDO <br>
Ay, my lord,<br>
I would know that.<br>
<br>
LORD POLONIUS <br>
Marry, sir, here's my drift;<br>
And I believe, it is a fetch of wit:<br>
You laying these slight sullies on my son,<br>
As 'twere a thing a little soil'd i' the working, Mark you,<br>
Your party in converse, him you would sound,<br>
Having ever seen in the prenominate crimes<br>
The youth you breathe of guilty, be assured<br>
He closes with you in this consequence;<br>
'Good sir,' or so, or 'friend,' or 'gentleman,'<br>
According to the phrase or the addition<br>
Of man and country.<br>
<br>
REYNALDO <br>
Very good, my lord.<br>
<br>
LORD POLONIUS <br>
And then, sir, does he this--he does--what was I<br>
about to say? By the mass, I was about to say<br>
something: where did I leave?<br>
<br>
REYNALDO <br>
At 'closes in the consequence,' at 'friend or so,'<br>
and 'gentleman.'<br>
<br>
LORD POLONIUS <br>
At 'closes in the consequence,' ay, marry;<br>
He closes thus: 'I know the gentleman;<br>
I saw him yesterday, or t' other day,<br>
Or then, or then; with such, or such; and, as you say,<br>
There was a' gaming; there o'ertook in's rouse;<br>
There falling out at tennis:' or perchance,<br>
'I saw him enter such a house of sale,'<br>
Videlicet, a brothel, or so forth.<br>
See you now;<br>
Your bait of falsehood takes this carp of truth:<br>
And thus do we of wisdom and of reach,<br>
With windlasses and with assays of bias,<br>
By indirections find directions out:<br>
So by my former lecture and advice,<br>
Shall you my son. You have me, have you not?<br>
<br>
REYNALDO <br>
My lord, I have.<br>
<br>
LORD POLONIUS <br>
God be wi' you; fare you well.<br>
<br>
REYNALDO <br>
Good my lord!<br>
<br>
LORD POLONIUS <br>
Observe his inclination in yourself.<br>
<br>
REYNALDO <br>
I shall, my lord.<br>
<br>
LORD POLONIUS <br>
And let him ply his music.<br>
<br>
REYNALDO <br>
Well, my lord.<br>
<br>
LORD POLONIUS <br>
Farewell!<br>
<br>
<i>Exit REYNALDO</i><br>
<br>
<i>Enter OPHELIA</i><br>
<br>
How now, Ophelia! what's the matter?<br>
<br>
OPHELIA <br>
O, my lord, my lord, I have been so affrighted!<br>
<br>
LORD POLONIUS <br>
With what, i' the name of God?<br>
<br>
OPHELIA <br>
My lord, as I was sewing in my closet,<br>
Lord Hamlet, with his doublet all unbraced;<br>
No hat upon his head; his stockings foul'd,<br>
Ungarter'd, and down-gyved to his ancle;<br>
Pale as his shirt; his knees knocking each other;<br>
And with a look so piteous in purport<br>
As if he had been loosed out of hell<br>
To speak of horrors,--he comes before me.<br>
<br>
LORD POLONIUS <br>
Mad for thy love?<br>
<br>
OPHELIA <br>
My lord, I do not know;<br>
But truly, I do fear it.<br>
<br>
LORD POLONIUS <br>
What said he?<br>
<br>
OPHELIA <br>
He took me by the wrist and held me hard;<br>
Then goes he to the length of all his arm;<br>
And, with his other hand thus o'er his brow,<br>
He falls to such perusal of my face<br>
As he would draw it. Long stay'd he so;<br>
At last, a little shaking of mine arm<br>
And thrice his head thus waving up and down,<br>
He raised a sigh so piteous and profound<br>
As it did seem to shatter all his bulk<br>
And end his being: that done, he lets me go:<br>
And, with his head over his shoulder turn'd,<br>
He seem'd to find his way without his eyes;<br>
For out o' doors he went without their helps,<br>
And, to the last, bended their light on me.<br>
<br>
LORD POLONIUS <br>
Come, go with me: I will go seek the king.<br>
This is the very ecstasy of love,<br>
Whose violent property fordoes itself<br>
And leads the will to desperate undertakings<br>
As oft as any passion under heaven<br>
That does afflict our natures. I am sorry.<br>
What, have you given him any hard words of late?<br>
<br>
OPHELIA <br>
No, my good lord, but, as you did command,<br>
I did repel his fetters and denied<br>
His access to me.<br>
<br>
LORD POLONIUS <br>
That hath made him mad.<br>
I am sorry that with better heed and judgment<br>
I had not quoted him: I fear'd he did but trifle,<br>
And meant to wreck thee; but, beshrew my jealousy!<br>
By heaven, it is as proper to our age<br>
To cast beyond ourselves in our opinions<br>
As it is common for the younger sort<br>
To lack discretion. Come, go we to the king:<br>
This must be known; which, being kept close, might<br>
move<br>
More grief to hide than hate to utter love.<br>
<br>
<i>Exeunt</i><br>
