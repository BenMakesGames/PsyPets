<i>Exit POLONIUS</i><br>
<br>
Leave me, friends.<br>
<br>
<i>Exeunt all but HAMLET</i><br>
<br>
Tis now the very witching time of night,<br>
When churchyards yawn and hell itself breathes out<br>
Contagion to this world: now could I drink hot blood,<br>
And do such bitter business as the day<br>
Would quake to look on. Soft! now to my mother.<br>
O heart, lose not thy nature; let not ever<br>
The soul of Nero enter this firm bosom:<br>
Let me be cruel, not unnatural:<br>
I will speak daggers to her, but use none;<br>
My tongue and soul in this be hypocrites;<br>
How in my words soever she be shent,<br>
To give them seals never, my soul, consent!<br>
<br>
<i>Exit</i><br>
