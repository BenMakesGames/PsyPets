SCENE VII. Another room in the castle.<br>
<br>
<i>Enter KING CLAUDIUS and LAERTES</i><br>
<br>
KING CLAUDIUS <br>
Now must your conscience my acquaintance seal,<br>
And you must put me in your heart for friend,<br>
Sith you have heard, and with a knowing ear,<br>
That he which hath your noble father slain<br>
Pursued my life.<br>
<br>
LAERTES <br>
It well appears: but tell me<br>
Why you proceeded not against these feats,<br>
So crimeful and so capital in nature,<br>
As by your safety, wisdom, all things else,<br>
You mainly were stirr'd up.<br>
<br>
KING CLAUDIUS <br>
O, for two special reasons;<br>
Which may to you, perhaps, seem much unsinew'd,<br>
But yet to me they are strong. The queen his mother<br>
Lives almost by his looks; and for myself--<br>
My virtue or my plague, be it either which--<br>
She's so conjunctive to my life and soul,<br>
That, as the star moves not but in his sphere,<br>
I could not but by her. The other motive,<br>
Why to a public count I might not go,<br>
Is the great love the general gender bear him;<br>
Who, dipping all his faults in their affection,<br>
Would, like the spring that turneth wood to stone,<br>
Convert his gyves to graces; so that my arrows,<br>
Too slightly timber'd for so loud a wind,<br>
Would have reverted to my bow again,<br>
And not where I had aim'd them.<br>
<br>
LAERTES <br>
And so have I a noble father lost;<br>
A sister driven into desperate terms,<br>
Whose worth, if praises may go back again,<br>
Stood challenger on mount of all the age<br>
For her perfections: but my revenge will come.<br>
<br>
KING CLAUDIUS <br>
Break not your sleeps for that: you must not think<br>
That we are made of stuff so flat and dull<br>
That we can let our beard be shook with danger<br>
And think it pastime. You shortly shall hear more:<br>
I loved your father, and we love ourself;<br>
And that, I hope, will teach you to imagine--<br>
<br>
<i>Enter a Messenger</i><br>
<br>
How now! what news?<br>
<br>
Messenger <br>
Letters, my lord, from Hamlet:<br>
This to your majesty; this to the queen.<br>
<br>
KING CLAUDIUS <br>
From Hamlet! who brought them?<br>
<br>
Messenger <br>
Sailors, my lord, they say; I saw them not:<br>
They were given me by Claudio; he received them<br>
Of him that brought them.<br>
<br>
KING CLAUDIUS <br>
Laertes, you shall hear them. Leave us.<br>
<br>
<i>Exit Messenger</i><br>
<br>
<i>Reads</i><br>
<br>
'High and mighty, You shall know I am set naked on<br>
your kingdom. To-morrow shall I beg leave to see<br>
your kingly eyes: when I shall, first asking your<br>
pardon thereunto, recount the occasion of my sudden<br>
and more strange return. 'HAMLET.'<br>
What should this mean? Are all the rest come back?<br>
Or is it some abuse, and no such thing?<br>
<br>
LAERTES <br>
Know you the hand?<br>
<br>
KING CLAUDIUS <br>
'Tis Hamlets character. 'Naked!<br>
And in a postscript here, he says 'alone.'<br>
Can you advise me?<br>
<br>
LAERTES <br>
I'm lost in it, my lord. But let him come;<br>
It warms the very sickness in my heart,<br>
That I shall live and tell him to his teeth,<br>
'Thus didest thou.'<br>
<br>
KING CLAUDIUS <br>
If it be so, Laertes--<br>
As how should it be so? how otherwise?--<br>
Will you be ruled by me?<br>
<br>
LAERTES <br>
Ay, my lord;<br>
So you will not o'errule me to a peace.<br>
<br>
KING CLAUDIUS <br>
To thine own peace. If he be now return'd,<br>
As checking at his voyage, and that he means<br>
No more to undertake it, I will work him<br>
To an exploit, now ripe in my device,<br>
Under the which he shall not choose but fall:<br>
And for his death no wind of blame shall breathe,<br>
But even his mother shall uncharge the practise<br>
And call it accident.<br>
<br>
LAERTES <br>
My lord, I will be ruled;<br>
The rather, if you could devise it so<br>
That I might be the organ.<br>
<br>
KING CLAUDIUS <br>
It falls right.<br>
You have been talk'd of since your travel much,<br>
And that in Hamlet's hearing, for a quality<br>
Wherein, they say, you shine: your sum of parts<br>
Did not together pluck such envy from him<br>
As did that one, and that, in my regard,<br>
Of the unworthiest siege.<br>
<br>
LAERTES <br>
What part is that, my lord?<br>
<br>
KING CLAUDIUS <br>
A very riband in the cap of youth,<br>
Yet needful too; for youth no less becomes<br>
The light and careless livery that it wears<br>
Than settled age his sables and his weeds,<br>
Importing health and graveness. Two months since,<br>
Here was a gentleman of Normandy:--<br>
I've seen myself, and served against, the French,<br>
And they can well on horseback: but this gallant<br>
Had witchcraft in't; he grew unto his seat;<br>
And to such wondrous doing brought his horse,<br>
As he had been incorpsed and demi-natured<br>
With the brave beast: so far he topp'd my thought,<br>
That I, in forgery of shapes and tricks,<br>
Come short of what he did.<br>
<br>
LAERTES <br>
A Norman was't?<br>
<br>
KING CLAUDIUS <br>
A Norman.<br>
<br>
LAERTES <br>
Upon my life, Lamond.<br>
<br>
KING CLAUDIUS <br>
The very same.<br>
<br>
LAERTES <br>
I know him well: he is the brooch indeed<br>
And gem of all the nation.<br>
<br>
KING CLAUDIUS <br>
He made confession of you,<br>
And gave you such a masterly report<br>
For art and exercise in your defence<br>
And for your rapier most especially,<br>
That he cried out, 'twould be a sight indeed,<br>
If one could match you: the scrimers of their nation,<br>
He swore, had had neither motion, guard, nor eye,<br>
If you opposed them. Sir, this report of his<br>
Did Hamlet so envenom with his envy<br>
That he could nothing do but wish and beg<br>
Your sudden coming o'er, to play with him.<br>
Now, out of this,--<br>
<br>
LAERTES <br>
What out of this, my lord?<br>
<br>
KING CLAUDIUS <br>
Laertes, was your father dear to you?<br>
Or are you like the painting of a sorrow,<br>
A face without a heart?<br>
<br>
LAERTES <br>
Why ask you this?<br>
<br>
KING CLAUDIUS <br>
Not that I think you did not love your father;<br>
But that I know love is begun by time;<br>
And that I see, in passages of proof,<br>
Time qualifies the spark and fire of it.<br>
There lives within the very flame of love<br>
A kind of wick or snuff that will abate it;<br>
And nothing is at a like goodness still;<br>
For goodness, growing to a plurisy,<br>
Dies in his own too much: that we would do<br>
We should do when we would; for this 'would' changes<br>
And hath abatements and delays as many<br>
As there are tongues, are hands, are accidents;<br>
And then this 'should' is like a spendthrift sigh,<br>
That hurts by easing. But, to the quick o' the ulcer:--<br>
Hamlet comes back: what would you undertake,<br>
To show yourself your father's son in deed<br>
More than in words?<br>
<br>
LAERTES <br>
To cut his throat i' the church.<br>
<br>
KING CLAUDIUS <br>
No place, indeed, should murder sanctuarize;<br>
Revenge should have no bounds. But, good Laertes,<br>
Will you do this, keep close within your chamber.<br>
Hamlet return'd shall know you are come home:<br>
We'll put on those shall praise your excellence<br>
And set a double varnish on the fame<br>
The Frenchman gave you, bring you in fine together<br>
And wager on your heads: he, being remiss,<br>
Most generous and free from all contriving,<br>
Will not peruse the foils; so that, with ease,<br>
Or with a little shuffling, you may choose<br>
A sword unbated, and in a pass of practise<br>
Requite him for your father.<br>
<br>
LAERTES <br>
I will do't:<br>
And, for that purpose, I'll anoint my sword.<br>
I bought an unction of a mountebank,<br>
So mortal that, but dip a knife in it,<br>
Where it draws blood no cataplasm so rare,<br>
Collected from all simples that have virtue<br>
Under the moon, can save the thing from death<br>
That is but scratch'd withal: I'll touch my point<br>
With this contagion, that, if I gall him slightly,<br>
It may be death.<br>
<br>
KING CLAUDIUS <br>
Let's further think of this;<br>
Weigh what convenience both of time and means<br>
May fit us to our shape: if this should fail,<br>
And that our drift look through our bad performance,<br>
'Twere better not assay'd: therefore this project<br>
Should have a back or second, that might hold,<br>
If this should blast in proof. Soft! let me see:<br>
We'll make a solemn wager on your cunnings: I ha't.<br>
When in your motion you are hot and dry--<br>
As make your bouts more violent to that end--<br>
And that he calls for drink, I'll have prepared him<br>
A chalice for the nonce, whereon but sipping,<br>
If he by chance escape your venom'd stuck,<br>
Our purpose may hold there.<br>
<br>
<i>Enter QUEEN GERTRUDE</i><br>
<br>
How now, sweet queen!<br>
<br>
QUEEN GERTRUDE <br>
One woe doth tread upon another's heel,<br>
So fast they follow; your sister's drown'd, Laertes.<br>
<br>
LAERTES <br>
Drown'd! O, where?<br>
<br>
QUEEN GERTRUDE <br>
There is a willow grows aslant a brook,<br>
That shows his hoar leaves in the glassy stream;<br>
There with fantastic garlands did she come<br>
Of crow-flowers, nettles, daisies, and long purples<br>
That liberal shepherds give a grosser name,<br>
But our cold maids do dead men's fingers call them:<br>
There, on the pendent boughs her coronet weeds<br>
Clambering to hang, an envious sliver broke;<br>
When down her weedy trophies and herself<br>
Fell in the weeping brook. Her clothes spread wide;<br>
And, mermaid-like, awhile they bore her up:<br>
Which time she chanted snatches of old tunes;<br>
As one incapable of her own distress,<br>
Or like a creature native and indued<br>
Unto that element: but long it could not be<br>
Till that her garments, heavy with their drink,<br>
Pull'd the poor wretch from her melodious lay<br>
To muddy death.<br>
<br>
LAERTES <br>
Alas, then, she is drown'd?<br>
<br>
QUEEN GERTRUDE <br>
Drown'd, drown'd.<br>
<br>
LAERTES <br>
Too much of water hast thou, poor Ophelia,<br>
And therefore I forbid my tears: but yet<br>
It is our trick; nature her custom holds,<br>
Let shame say what it will: when these are gone,<br>
The woman will be out. Adieu, my lord:<br>
I have a speech of fire, that fain would blaze,<br>
But that this folly douts it.<br>
<br>
<i>Exit</i><br>
<br>
KING CLAUDIUS <br>
Let's follow, Gertrude:<br>
How much I had to do to calm his rage!<br>
Now fear I this will give it start again;<br>
Therefore let's follow.<br>
<br>
<i>Exeunt</i>
