SCENE V. Elsinore. A room in the castle.<br>
<br>
<i>Enter QUEEN GERTRUDE, HORATIO, and a Gentleman</i><br>
QUEEN GERTRUDE <br>
I will not speak with her.<br>
<br>
Gentleman <br>
She is importunate, indeed distract:<br>
Her mood will needs be pitied.<br>
<br>
QUEEN GERTRUDE <br>
What would she have?<br>
<br>
Gentleman <br>
She speaks much of her father; says she hears<br>
There's tricks i' the world; and hems, and beats her heart;<br>
Spurns enviously at straws; speaks things in doubt,<br>
That carry but half sense: her speech is nothing,<br>
Yet the unshaped use of it doth move<br>
The hearers to collection; they aim at it,<br>
And botch the words up fit to their own thoughts;<br>
Which, as her winks, and nods, and gestures<br>
yield them,<br>
Indeed would make one think there might be thought,<br>
Though nothing sure, yet much unhappily.<br>
<br>
HORATIO <br>
'Twere good she were spoken with; for she may strew<br>
Dangerous conjectures in ill-breeding minds.<br>
<br>
QUEEN GERTRUDE <br>
Let her come in.<br>
<br>
<i>Exit HORATIO</i><br>
<br>
To my sick soul, as sin's true nature is,<br>
Each toy seems prologue to some great amiss:<br>
So full of artless jealousy is guilt,<br>
It spills itself in fearing to be spilt.<br>
<br>
<i>Re-enter HORATIO, with OPHELIA</i><br>
<br>
OPHELIA <br>
Where is the beauteous majesty of Denmark?<br>
<br>
QUEEN GERTRUDE <br>
How now, Ophelia!<br>
<br>
OPHELIA <br>
<i>Sings</i><br>
<br>
How should I your true love know<br>
From another one?<br>
By his cockle hat and staff,<br>
And his sandal shoon.<br>
<br>
QUEEN GERTRUDE <br>
Alas, sweet lady, what imports this song?<br>
<br>
OPHELIA <br>
Say you? nay, pray you, mark.<br>
<br>
<i>Sings</i><br>
<br>
He is dead and gone, lady,<br>
He is dead and gone;<br>
At his head a grass-green turf,<br>
At his heels a stone.<br>
<br>
QUEEN GERTRUDE <br>
Nay, but, Ophelia,--<br>
<br>
OPHELIA <br>
Pray you, mark.<br>
<br>
<i>Sings</i><br>
<br>
White his shroud as the mountain snow,--<br>
<br>
<i>Enter KING CLAUDIUS</i><br>
<br>
QUEEN GERTRUDE <br>
Alas, look here, my lord.<br>
<br>
OPHELIA <br>
<i>Sings</i><br>
<br>
Larded with sweet flowers<br>
Which bewept to the grave did go<br>
With true-love showers.<br>
<br>
KING CLAUDIUS <br>
How do you, pretty lady?<br>
<br>
OPHELIA <br>
Well, God 'ild you! They say the owl was a baker's<br>
daughter. Lord, we know what we are, but know not<br>
what we may be. God be at your table!<br>
<br>
KING CLAUDIUS <br>
Conceit upon her father.<br>
<br>
OPHELIA <br>
Pray you, let's have no words of this; but when they<br>
ask you what it means, say you this:<br>
<br>
<i>Sings</i><br>
<br>
To-morrow is Saint Valentine's day,<br>
All in the morning betime,<br>
And I a maid at your window,<br>
To be your Valentine.<br>
Then up he rose, and donn'd his clothes,<br>
And dupp'd the chamber-door;<br>
Let in the maid, that out a maid<br>
Never departed more.<br>
<br>
KING CLAUDIUS <br>
Pretty Ophelia!<br>
<br>
OPHELIA <br>
Indeed, la, without an oath, I'll make an end on't:<br>
<br>
<i>Sings</i><br>
<br>
By Gis and by Saint Charity,<br>
Alack, and fie for shame!<br>
Young men will do't, if they come to't;<br>
By cock, they are to blame.<br>
Quoth she, before you tumbled me,<br>
You promised me to wed.<br>
So would I ha' done, by yonder sun,<br>
An thou hadst not come to my bed.<br>
<br>
KING CLAUDIUS <br>
How long hath she been thus?<br>
<br>
OPHELIA <br>
I hope all will be well. We must be patient: but I<br>
cannot choose but weep, to think they should lay him<br>
i' the cold ground. My brother shall know of it:<br>
and so I thank you for your good counsel. Come, my<br>
coach! Good night, ladies; good night, sweet ladies;<br>
good night, good night.<br>
<br>
<i>Exit</i><br>
<br>
KING CLAUDIUS <br>
Follow her close; give her good watch,<br>
I pray you.<br>
<br>
<i>Exit HORATIO</i><br>
<br>
O, this is the poison of deep grief; it springs<br>
All from her father's death. O Gertrude, Gertrude,<br>
When sorrows come, they come not single spies<br>
But in battalions. First, her father slain:<br>
Next, your son gone; and he most violent author<br>
Of his own just remove: the people muddied,<br>
Thick and unwholesome in their thoughts and whispers,<br>
For good Polonius' death; and we have done but greenly,<br>
In hugger-mugger to inter him: poor Ophelia<br>
Divided from herself and her fair judgment,<br>
Without the which we are pictures, or mere beasts:<br>
Last, and as much containing as all these,<br>
Her brother is in secret come from France;<br>
Feeds on his wonder, keeps himself in clouds,<br>
And wants not buzzers to infect his ear<br>
With pestilent speeches of his father's death;<br>
Wherein necessity, of matter beggar'd,<br>
Will nothing stick our person to arraign<br>
In ear and ear. O my dear Gertrude, this,<br>
Like to a murdering-piece, in many places<br>
Gives me superfluous death.<br>
<br>
<i>A noise within</i><br>
<br>
QUEEN GERTRUDE <br>
Alack, what noise is this?<br>
<br>
KING CLAUDIUS <br>
Where are my Switzers? Let them guard the door.<br>
<br>
<i>Enter another Gentleman</i><br>
<br>
What is the matter?<br>
<br>
Gentleman <br>
Save yourself, my lord:<br>
The ocean, overpeering of his list,<br>
Eats not the flats with more impetuous haste<br>
Than young Laertes, in a riotous head,<br>
O'erbears your officers. The rabble call him lord;<br>
And, as the world were now but to begin,<br>
Antiquity forgot, custom not known,<br>
The ratifiers and props of every word,<br>
They cry 'Choose we: Laertes shall be king:'<br>
Caps, hands, and tongues, applaud it to the clouds:<br>
'Laertes shall be king, Laertes king!'<br>
<br>
QUEEN GERTRUDE <br>
How cheerfully on the false trail they cry!<br>
O, this is counter, you false Danish dogs!<br>
<br>
KING CLAUDIUS <br>
The doors are broke.<br>
<br>
<i>Noise within</i><br>
<br>
<i>Enter LAERTES, armed; Danes following</i><br>
<br>
LAERTES <br>
Where is this king? Sirs, stand you all without.<br>
<br>
Danes <br>
No, let's come in.<br>
<br>
LAERTES <br>
I pray you, give me leave.<br>
<br>
Danes <br>
We will, we will.<br>
<br>
<i>They retire without the door</i><br>
<br>
LAERTES <br>
I thank you: keep the door. O thou vile king,<br>
Give me my father!<br>
<br>
QUEEN GERTRUDE <br>
Calmly, good Laertes.<br>
<br>
LAERTES <br>
That drop of blood that's calm proclaims me bastard,<br>
Cries cuckold to my father, brands the harlot<br>
Even here, between the chaste unsmirched brow<br>
Of my true mother.<br>
<br>
KING CLAUDIUS <br>
What is the cause, Laertes,<br>
That thy rebellion looks so giant-like?<br>
Let him go, Gertrude; do not fear our person:<br>
There's such divinity doth hedge a king,<br>
That treason can but peep to what it would,<br>
Acts little of his will. Tell me, Laertes,<br>
Why thou art thus incensed. Let him go, Gertrude.<br>
Speak, man.<br>
<br>
LAERTES <br>
Where is my father?<br>
<br>
KING CLAUDIUS <br>
Dead.<br>
<br>
QUEEN GERTRUDE <br>
But not by him.<br>
<br>
KING CLAUDIUS <br>
Let him demand his fill.<br>
<br>
LAERTES <br>
How came he dead? I'll not be juggled with:<br>
To hell, allegiance! vows, to the blackest devil!<br>
Conscience and grace, to the profoundest pit!<br>
I dare damnation. To this point I stand,<br>
That both the worlds I give to negligence,<br>
Let come what comes; only I'll be revenged<br>
Most thoroughly for my father.<br>
<br>
KING CLAUDIUS <br>
Who shall stay you?<br>
<br>
LAERTES <br>
My will, not all the world:<br>
And for my means, I'll husband them so well,<br>
They shall go far with little.<br>
<br>
KING CLAUDIUS <br>
Good Laertes,<br>
If you desire to know the certainty<br>
Of your dear father's death, is't writ in your revenge,<br>
That, swoopstake, you will draw both friend and foe,<br>
Winner and loser?<br>
<br>
LAERTES <br>
None but his enemies.<br>
<br>
KING CLAUDIUS <br>
Will you know them then?<br>
<br>
LAERTES <br>
To his good friends thus wide I'll ope my arms;<br>
And like the kind life-rendering pelican,<br>
Repast them with my blood.<br>
<br>
KING CLAUDIUS <br>
Why, now you speak<br>
Like a good child and a true gentleman.<br>
That I am guiltless of your father's death,<br>
And am most sensible in grief for it,<br>
It shall as level to your judgment pierce<br>
As day does to your eye.<br>
<br>
Danes <br>
[Within] Let her come in.<br>
<br>
LAERTES <br>
How now! what noise is that?<br>
<br>
<i>Re-enter OPHELIA</i><br>
<br>
O heat, dry up my brains! tears seven times salt,<br>
Burn out the sense and virtue of mine eye!<br>
By heaven, thy madness shall be paid by weight,<br>
Till our scale turn the beam. O rose of May!<br>
Dear maid, kind sister, sweet Ophelia!<br>
O heavens! is't possible, a young maid's wits<br>
Should be as moral as an old man's life?<br>
Nature is fine in love, and where 'tis fine,<br>
It sends some precious instance of itself<br>
After the thing it loves.<br>
<br>
OPHELIA <br>
<i>Sings</i><br>
<br>
They bore him barefaced on the bier;<br>
Hey non nonny, nonny, hey nonny;<br>
And in his grave rain'd many a tear:--<br>
Fare you well, my dove!<br>
<br>
LAERTES <br>
Hadst thou thy wits, and didst persuade revenge,<br>
It could not move thus.<br>
<br>
OPHELIA <br>
<i>Sings</i><br>
<br>
You must sing a-down a-down,<br>
An you call him a-down-a.<br>
O, how the wheel becomes it! It is the false<br>
steward, that stole his master's daughter.<br>
<br>
LAERTES <br>
This nothing's more than matter.<br>
<br>
OPHELIA <br>
There's rosemary, that's for remembrance; pray,<br>
love, remember: and there is pansies. that's for thoughts.<br>
<br>
LAERTES <br>
A document in madness, thoughts and remembrance fitted.<br>
<br>
OPHELIA <br>
There's fennel for you, and columbines: there's rue<br>
for you; and here's some for me: we may call it<br>
herb-grace o' Sundays: O you must wear your rue with<br>
a difference. There's a daisy: I would give you<br>
some violets, but they withered all when my father<br>
died: they say he made a good end,--<br>
<br>
<i>Sings</i><br>
<br>
For bonny sweet Robin is all my joy.<br>
<br>
LAERTES <br>
Thought and affliction, passion, hell itself,<br>
She turns to favour and to prettiness.<br>
<br>
OPHELIA <br>
<i>Sings</i><br>
<br>
And will he not come again?<br>
And will he not come again?<br>
No, no, he is dead:<br>
Go to thy death-bed:<br>
He never will come again.<br>
His beard was as white as snow,<br>
All flaxen was his poll:<br>
He is gone, he is gone,<br>
And we cast away moan:<br>
God ha' mercy on his soul!<br>
And of all Christian souls, I pray God. God be wi' ye.<br>
<br>
<i>Exit</i><br>
<br>
LAERTES <br>
Do you see this, O God?<br>
<br>
KING CLAUDIUS <br>
Laertes, I must commune with your grief,<br>
Or you deny me right. Go but apart,<br>
Make choice of whom your wisest friends you will.<br>
And they shall hear and judge 'twixt you and me:<br>
If by direct or by collateral hand<br>
They find us touch'd, we will our kingdom give,<br>
Our crown, our life, and all that we can ours,<br>
To you in satisfaction; but if not,<br>
Be you content to lend your patience to us,<br>
And we shall jointly labour with your soul<br>
To give it due content.<br>
<br>
LAERTES <br>
Let this be so;<br>
His means of death, his obscure funeral--<br>
No trophy, sword, nor hatchment o'er his bones,<br>
No noble rite nor formal ostentation--<br>
Cry to be heard, as 'twere from heaven to earth,<br>
That I must call't in question.<br>
<br>
KING CLAUDIUS <br>
So you shall;<br>
And where the offence is let the great axe fall.<br>
I pray you, go with me.<br>
<br>
<i>Exeunt</i><br>
