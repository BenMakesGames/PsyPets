<b>Love Spell</b><br>
You will need:<br>
Redsberry Wine<br>
Gold Ring<br>
<br>
This spell is intended to open the caster’s heart to love. It is NOT for influencing a specific pet’s or person’s feelings.<br>
<br>
Under a full moon, take the gold ring and hold it up to the moonlight. Envisage a pink light encircling the ring, causing it to glow and shimmer. Now focus on the light moving in a deosil direction and imagine the light spreading down through your arms and body. As the light swirls and pulses, whisper the following three times:<br>
<br>
<i>Like the line of a ring that is round and whole,</i><br>
<i>Let love fill the spaces in my soul.</i><br>
<br>
Take the ring and bury it under the Giving Tree, or if you cannot reach the Giving Tree, then bury it in a pot that contains an Amethyst Rose. Pour some Redsberry Wine on the spot and then touch the blade of a consecrated Vorpal Sword to the earth. Picture the pink light spreading up into the roots of the plant and along the branches, then announce in a loud voice:<br>
<br>
<i>Spread, oh Light of Love, into this Tree,</i><br>
<i>And when thou blossom, return to me times three.</i><br>
