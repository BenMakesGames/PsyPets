<p><b>Simple Circuit</b></p>
<p>While the Simple Circuit does little more than turn on and off, it is a vital component in almost every device you will make.</p>
<p>Construction of the Simple Circuit is straightforward: etch the Copper paths into a Plastic breadboard.</p>
<p><b>Disco Ball</b></p>
<p>While the electronic components of the Disco Ball are simple enough (consisting of only a Simple Circuit), the ball itself requires an amount of work, and supplies.</p>
<p>To make the mirror components, you will need to coat Glass with Silver.  Three pieces of Glass should be enough to make the ball, and one piece of Silver alone will easily coat all three.  You will also need to etch color into the Glass.  For this, a Red Dye, Blue Dye and Yellow Dye will do the job.</p>
<p>While not particularly useful on its own, the Disco Ball can make a good diversion, giving you an opportunity to fulfill your part of the plans.</p>
<p><b>4-Function Calculator</b></p>
<p>4-Function Calculators (and its only slightly more complicated 5-Function brethern) are a simple construction.  A slightly-modified Simple Circuit is capable of addition - the rest is just programming.  Add a Plastic case and a Glass display screen, and you're done.</p>
<p>Use the Calculator to solve trickier problems, but be sure to check your work!  Nothing's more embarrassing than lasering the wrong country.</p>
<p><b>Complex Circuit</b></p>
<p>More complicated devices require more complex circuitry.  The Complex Circuit is capable of storing small amounts of data and code.  It can even keep time.</p>
<p>Similar to the Simple Circuit, we will lay Copper paths onto a Plastic breadboard.  In order to facilitate the additional functions within the same amount of space, however, this board will be double-layered, doubling the amount of Copper and Plastic needed (to two each).  Add a Small Giamond to keep the time, and you're done.</p>
<p><b>Hangman Minigame</b></p>
<p>Once you have the fancy Plastic and Glass casing and buttons made, slide in a Simple Circuit and a Complex Circuit which has had the Hangman game and dictionary programmed in to it.</p>
<p>The Hangman Minigame, when carefully modified, can be used to send secret messages through dangerous territory!  Simply modify the dictionary to contain the code word you want sent.</p>