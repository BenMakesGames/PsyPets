<p>Corn Syrup: A great source of sugar for many candies.  Let's examine a few of its uses, shall we?</p>
<h5>Twists</h5>
<p>Candy "Twists" are as delicious as they are easy to prepare.</p>
<p>You will need:</p>
<ul>
 <li>Corn Syrup</li>
 <li>Corn Starch</li>
 <li>Flour</li>
 <li>Sugar</li>
 <li>Redsberries</li>
</ul>
<p>For other great flavors, try replacing the Redsberries with Blueberries, or even Chocolate!</p>
<h5>Pineapple Drops</h5>
<p>Pineapple Drops are a hard candy.  You have to bring Sugar and Corn Syrup to nearly 300 degrees!</p>
<p>Sounds intimidating, but it's not that difficult.</p>
<p>You will need:</p>
<ul>
 <li>Sugar</li>
 <li>Corn Syrup</li>
 <li>Pineapple</li>
</ul>
<p>Don't be afraid to try other flavors, too!</p>
