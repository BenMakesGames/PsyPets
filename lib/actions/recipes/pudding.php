<b>Puddings: An Introduction</b><br />
<br />
In this book we'll cover three kinds of puddings: rice, bread, and chocolate.<br />
<br />
Bread is easiest, so we'll start with that.  Use bread that's been left out for a day for best results.<br />
<br />
<b>Bread Pudding</b><br />
<br />
Preheat your oven.<br />
Break the White Bread into small pieces into a baking pan.  Drizzle melted Butter over the bread and pour in a mixture of Egg, Milk and Sugar.  Press it all with a fork to make sure everything soaks together nicely.<br />
Bake until the top springs back when lightly tapped.<br />
<br />
Serves four.<br />
<br />
<b>Rice Pudding</b><br />
<br />
Combine Milk, Sugar, Egg and Rice in a saucepan and let simmer, stirring frequently.<br />
Pour it into a dish and cover it with plastic wrap.  Poke some holes to let the heat escape.<br />
Let cool eight hours (say, overnight).<br />
<br />
Serves four.<br />
<br />
<b>Chocolate Pudding</b><br />
<br />
This is the most complicated of the recipes.<br />
You may also include Egg at your option.  Doing so increases the yield by one serving.<br />
<br />
Combine Milk, Sugar, Baking Chocolate and Cornstarch in a saucepan.  Stir over low heat until thickened and smooth.<br />
<br />
Serves two.<br />
