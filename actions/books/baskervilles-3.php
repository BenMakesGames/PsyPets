<h5>Chapter 3: The Problem</h5>

<p>I confess at these words a shudder passed through me. There was a thrill
in the doctor's voice which showed that he was himself deeply moved by
that which he told us. Holmes leaned forward in his excitement and his
eyes had the hard, dry glitter which shot from them when he was keenly
interested.</p>

<p>"You saw this?"</p>

<p>"As clearly as I see you."</p>

<p>"And you said nothing?"</p>

<p>"What was the use?"</p>

<p>"How was it that no one else saw it?"</p>

<p>"The marks were some twenty yards from the body and no one gave them
a thought. I don't suppose I should have done so had I not known this
legend."</p>

<p>"There are many sheep-dogs on the moor?"</p>

<p>"No doubt, but this was no sheep-dog."</p>

<p>"You say it was large?"</p>

<p>"Enormous."</p>

<p>"But it had not approached the body?"</p>

<p>"No."</p>

<p>"What sort of night was it?"</p>

<p>"Damp and raw."</p>

<p>"But not actually raining?"</p>

<p>"No."</p>

<p>"What is the alley like?"</p>

<p>"There are two lines of old yew hedge, twelve feet high and
impenetrable. The walk in the centre is about eight feet across."</p>

<p>"Is there anything between the hedges and the walk?"</p>

<p>"Yes, there is a strip of grass about six feet broad on either side."</p>

<p>"I understand that the yew hedge is penetrated at one point by a gate?"</p>

<p>"Yes, the wicket-gate which leads on to the moor."</p>

<p>"Is there any other opening?"</p>

<p>"None."</p>

<p>"So that to reach the yew alley one either has to come down it from the
house or else to enter it by the moor-gate?"</p>

<p>"There is an exit through a summer-house at the far end."</p>

<p>"Had Sir Charles reached this?"</p>

<p>"No; he lay about fifty yards from it."</p>

<p>"Now, tell me, Dr. Mortimer--and this is important--the marks which you
saw were on the path and not on the grass?"</p>

<p>"No marks could show on the grass."</p>

<p>"Were they on the same side of the path as the moor-gate?"</p>

<p>"Yes; they were on the edge of the path on the same side as the
moor-gate."</p>

<p>"You interest me exceedingly. Another point. Was the wicket-gate
closed?"</p>

<p>"Closed and padlocked."</p>

<p>"How high was it?"</p>

<p>"About four feet high."</p>

<p>"Then anyone could have got over it?"</p>

<p>"Yes."</p>

<p>"And what marks did you see by the wicket-gate?"</p>

<p>"None in particular."</p>

<p>"Good heaven! Did no one examine?"</p>

<p>"Yes, I examined, myself."</p>

<p>"And found nothing?"</p>

<p>"It was all very confused. Sir Charles had evidently stood there for
five or ten minutes."</p>

<p>"How do you know that?"</p>

<p>"Because the ash had twice dropped from his cigar."</p>

<p>"Excellent! This is a colleague, Watson, after our own heart. But the
marks?"</p>

<p>"He had left his own marks all over that small patch of gravel. I could
discern no others."</p>

<p>Sherlock Holmes struck his hand against his knee with an impatient
gesture.</p>

<p>"If I had only been there!" he cried. "It is evidently a case of
extraordinary interest, and one which presented immense opportunities to
the scientific expert. That gravel page upon which I might have read so
much has been long ere this smudged by the rain and defaced by the clogs
of curious peasants. Oh, Dr. Mortimer, Dr. Mortimer, to think that you
should not have called me in! You have indeed much to answer for."</p>

<p>"I could not call you in, Mr. Holmes, without disclosing these facts to
the world, and I have already given my reasons for not wishing to do so.
Besides, besides--"</p>

<p>"Why do you hesitate?"</p>

<p>"There is a realm in which the most acute and most experienced of
detectives is helpless."</p>

<p>"You mean that the thing is supernatural?"</p>

<p>"I did not positively say so."</p>

<p>"No, but you evidently think it."</p>

<p>"Since the tragedy, Mr. Holmes, there have come to my ears several
incidents which are hard to reconcile with the settled order of Nature."</p>

<p>"For example?"</p>

<p>"I find that before the terrible event occurred several people had seen
a creature upon the moor which corresponds with this Baskerville demon,
and which could not possibly be any animal known to science. They all
agreed that it was a huge creature, luminous, ghastly, and spectral. I
have cross-examined these men, one of them a hard-headed countryman,
one a farrier, and one a moorland farmer, who all tell the same story of
this dreadful apparition, exactly corresponding to the hell-hound of the
legend. I assure you that there is a reign of terror in the district,
and that it is a hardy man who will cross the moor at night."</p>

<p>"And you, a trained man of science, believe it to be supernatural?"</p>

<p>"I do not know what to believe."</p>

<p>Holmes shrugged his shoulders. "I have hitherto confined my
investigations to this world," said he. "In a modest way I have combated
evil, but to take on the Father of Evil himself would, perhaps, be too
ambitious a task. Yet you must admit that the footmark is material."</p>

<p>"The original hound was material enough to tug a man's throat out, and
yet he was diabolical as well."</p>

<p>"I see that you have quite gone over to the supernaturalists. But now,
Dr. Mortimer, tell me this. If you hold these views, why have you come
to consult me at all? You tell me in the same breath that it is useless
to investigate Sir Charles's death, and that you desire me to do it."</p>

<p>"I did not say that I desired you to do it."</p>

<p>"Then, how can I assist you?"</p>

<p>"By advising me as to what I should do with Sir Henry Baskerville, who
arrives at Waterloo Station"--Dr. Mortimer looked at his watch--"in
exactly one hour and a quarter."</p>

<p>"He being the heir?"</p>

<p>"Yes. On the death of Sir Charles we inquired for this young gentleman
and found that he had been farming in Canada. From the accounts which
have reached us he is an excellent fellow in every way. I speak now not
as a medical man but as a trustee and executor of Sir Charles's will."</p>

<p>"There is no other claimant, I presume?"</p>

<p>"None. The only other kinsman whom we have been able to trace was Rodger
Baskerville, the youngest of three brothers of whom poor Sir Charles was
the elder. The second brother, who died young, is the father of this lad
Henry. The third, Rodger, was the black sheep of the family. He came of
the old masterful Baskerville strain and was the very image, they tell
me, of the family picture of old Hugo. He made England too hot to hold
him, fled to Central America, and died there in 1876 of yellow fever.
Henry is the last of the Baskervilles. In one hour and five minutes
I meet him at Waterloo Station. I have had a wire that he arrived at
Southampton this morning. Now, Mr. Holmes, what would you advise me to
do with him?"</p>

<p>"Why should he not go to the home of his fathers?"</p>

<p>"It seems natural, does it not? And yet, consider that every Baskerville
who goes there meets with an evil fate. I feel sure that if Sir Charles
could have spoken with me before his death he would have warned me
against bringing this, the last of the old race, and the heir to great
wealth, to that deadly place. And yet it cannot be denied that the
prosperity of the whole poor, bleak countryside depends upon his
presence. All the good work which has been done by Sir Charles will
crash to the ground if there is no tenant of the Hall. I fear lest I
should be swayed too much by my own obvious interest in the matter, and
that is why I bring the case before you and ask for your advice."</p>

<p>Holmes considered for a little time.</p>

<p>"Put into plain words, the matter is this," said he. "In your opinion
there is a diabolical agency which makes Dartmoor an unsafe abode for a
Baskerville--that is your opinion?"</p>

<p>"At least I might go the length of saying that there is some evidence
that this may be so."</p>

<p>"Exactly. But surely, if your supernatural theory be correct, it could
work the young man evil in London as easily as in Devonshire. A devil
with merely local powers like a parish vestry would be too inconceivable
a thing."</p>

<p>"You put the matter more flippantly, Mr. Holmes, than you would probably
do if you were brought into personal contact with these things. Your
advice, then, as I understand it, is that the young man will be as safe
in Devonshire as in London. He comes in fifty minutes. What would you
recommend?"</p>

<p>"I recommend, sir, that you take a cab, call off your spaniel who is
scratching at my front door, and proceed to Waterloo to meet Sir Henry
Baskerville."</p>

<p>"And then?"</p>

<p>"And then you will say nothing to him at all until I have made up my
mind about the matter."</p>

<p>"How long will it take you to make up your mind?"</p>

<p>"Twenty-four hours. At ten o'clock tomorrow, Dr. Mortimer, I will be
much obliged to you if you will call upon me here, and it will be
of help to me in my plans for the future if you will bring Sir Henry
Baskerville with you."</p>

<p>"I will do so, Mr. Holmes." He scribbled the appointment on his
shirt-cuff and hurried off in his strange, peering, absent-minded
fashion. Holmes stopped him at the head of the stair.</p>

<p>"Only one more question, Dr. Mortimer. You say that before Sir Charles
Baskerville's death several people saw this apparition upon the moor?"</p>

<p>"Three people did."</p>

<p>"Did any see it after?"</p>

<p>"I have not heard of any."</p>

<p>"Thank you. Good-morning."</p>

<p>Holmes returned to his seat with that quiet look of inward satisfaction
which meant that he had a congenial task before him.</p>

<p>"Going out, Watson?"</p>

<p>"Unless I can help you."</p>

<p>"No, my dear fellow, it is at the hour of action that I turn to you for
aid. But this is splendid, really unique from some points of view.
When you pass Bradley's, would you ask him to send up a pound of the
strongest shag tobacco? Thank you. It would be as well if you could make
it convenient not to return before evening. Then I should be very glad
to compare impressions as to this most interesting problem which has
been submitted to us this morning."</p>

<p>I knew that seclusion and solitude were very necessary for my friend
in those hours of intense mental concentration during which he weighed
every particle of evidence, constructed alternative theories, balanced
one against the other, and made up his mind as to which points were
essential and which immaterial. I therefore spent the day at my club and
did not return to Baker Street until evening. It was nearly nine o'clock
when I found myself in the sitting-room once more.</p>

<p>My first impression as I opened the door was that a fire had broken out,
for the room was so filled with smoke that the light of the lamp upon
the table was blurred by it. As I entered, however, my fears were set at
rest, for it was the acrid fumes of strong coarse tobacco which took me
by the throat and set me coughing. Through the haze I had a vague vision
of Holmes in his dressing-gown coiled up in an armchair with his black
clay pipe between his lips. Several rolls of paper lay around him.</p>

<p>"Caught cold, Watson?" said he.</p>

<p>"No, it's this poisonous atmosphere."</p>

<p>"I suppose it is pretty thick, now that you mention it."</p>

<p>"Thick! It is intolerable."</p>

<p>"Open the window, then! You have been at your club all day, I perceive."</p>

<p>"My dear Holmes!"</p>

<p>"Am I right?"</p>

<p>"Certainly, but how?"</p>

<p>He laughed at my bewildered expression. "There is a delightful freshness
about you, Watson, which makes it a pleasure to exercise any small
powers which I possess at your expense. A gentleman goes forth on a
showery and miry day. He returns immaculate in the evening with the
gloss still on his hat and his boots. He has been a fixture therefore
all day. He is not a man with intimate friends. Where, then, could he
have been? Is it not obvious?"</p>

<p>"Well, it is rather obvious."</p>

<p>"The world is full of obvious things which nobody by any chance ever
observes. Where do you think that I have been?"</p>

<p>"A fixture also."</p>

<p>"On the contrary, I have been to Devonshire."</p>

<p>"In spirit?"</p>

<p>"Exactly. My body has remained in this armchair and has, I regret
to observe, consumed in my absence two large pots of coffee and an
incredible amount of tobacco. After you left I sent down to Stamford's
for the Ordnance map of this portion of the moor, and my spirit has
hovered over it all day. I flatter myself that I could find my way
about."</p>

<p>"A large-scale map, I presume?"</p>

<p>"Very large."</p>

<p>He unrolled one section and held it over his knee. "Here you have the
particular district which concerns us. That is Baskerville Hall in the
middle."</p>

<p>"With a wood round it?"</p>

<p>"Exactly. I fancy the yew alley, though not marked under that name, must
stretch along this line, with the moor, as you perceive, upon the right
of it. This small clump of buildings here is the hamlet of Grimpen,
where our friend Dr. Mortimer has his headquarters. Within a radius of
five miles there are, as you see, only a very few scattered dwellings.
Here is Lafter Hall, which was mentioned in the narrative. There is
a house indicated here which may be the residence of the
naturalist--Stapleton, if I remember right, was his name. Here are two
moorland farmhouses, High Tor and Foulmire. Then fourteen miles away the
great convict prison of Princetown. Between and around these scattered
points extends the desolate, lifeless moor. This, then, is the stage
upon which tragedy has been played, and upon which we may help to play
it again."</p>

<p>"It must be a wild place."</p>

<p>"Yes, the setting is a worthy one. If the devil did desire to have a
hand in the affairs of men--"</p>

<p>"Then you are yourself inclining to the supernatural explanation."</p>

<p>"The devil's agents may be of flesh and blood, may they not? There are
two questions waiting for us at the outset. The one is whether any crime
has been committed at all; the second is, what is the crime and how was
it committed? Of course, if Dr. Mortimer's surmise should be correct,
and we are dealing with forces outside the ordinary laws of Nature,
there is an end of our investigation. But we are bound to exhaust all
other hypotheses before falling back upon this one. I think we'll shut
that window again, if you don't mind. It is a singular thing, but I find
that a concentrated atmosphere helps a concentration of thought. I have
not pushed it to the length of getting into a box to think, but that is
the logical outcome of my convictions. Have you turned the case over in
your mind?"</p>

<p>"Yes, I have thought a good deal of it in the course of the day."</p>

<p>"What do you make of it?"</p>

<p>"It is very bewildering."</p>

<p>"It has certainly a character of its own. There are points of
distinction about it. That change in the footprints, for example. What
do you make of that?"</p>

<p>"Mortimer said that the man had walked on tiptoe down that portion of
the alley."</p>

<p>"He only repeated what some fool had said at the inquest. Why should a
man walk on tiptoe down the alley?"</p>

<p>"What then?"</p>

<p>"He was running, Watson--running desperately, running for his life,
running until he burst his heart--and fell dead upon his face."</p>

<p>"Running from what?"</p>

<p>"There lies our problem. There are indications that the man was crazed
with fear before ever he began to run."</p>

<p>"How can you say that?"</p>

<p>"I am presuming that the cause of his fears came to him across the moor.
If that were so, and it seems most probable, only a man who had lost his
wits would have run from the house instead of towards it. If the
gipsy's evidence may be taken as true, he ran with cries for help in the
direction where help was least likely to be. Then, again, whom was he
waiting for that night, and why was he waiting for him in the yew alley
rather than in his own house?"</p>

<p>"You think that he was waiting for someone?"</p>

<p>"The man was elderly and infirm. We can understand his taking an evening
stroll, but the ground was damp and the night inclement. Is it natural
that he should stand for five or ten minutes, as Dr. Mortimer, with more
practical sense than I should have given him credit for, deduced from
the cigar ash?"</p>

<p>"But he went out every evening."</p>

<p>"I think it unlikely that he waited at the moor-gate every evening. On
the contrary, the evidence is that he avoided the moor. That night he
waited there. It was the night before he made his departure for London.
The thing takes shape, Watson. It becomes coherent. Might I ask you to
hand me my violin, and we will postpone all further thought upon this
business until we have had the advantage of meeting Dr. Mortimer and Sir
Henry Baskerville in the morning."</p>
