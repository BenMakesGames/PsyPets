SCENE I. A room in the castle.<br>
<br>
<i>Enter KING CLAUDIUS, QUEEN GERTRUDE, ROSENCRANTZ, and GUILDENSTERN</i><br>
<br>
KING CLAUDIUS <br>
There's matter in these sighs, these profound heaves:<br>
You must translate: 'tis fit we understand them.<br>
Where is your son?<br>
<br>
QUEEN GERTRUDE <br>
Bestow this place on us a little while.<br>
<br>
<i>Exeunt ROSENCRANTZ and GUILDENSTERN</i><br>
<br>
Ah, my good lord, what have I seen to-night!<br>
<br>
KING CLAUDIUS <br>
What, Gertrude? How does Hamlet?<br>
<br>
QUEEN GERTRUDE <br>
Mad as the sea and wind, when both contend<br>
Which is the mightier: in his lawless fit,<br>
Behind the arras hearing something stir,<br>
Whips out his rapier, cries, 'A rat, a rat!'<br>
And, in this brainish apprehension, kills<br>
The unseen good old man.<br>
<br>
KING CLAUDIUS <br>
O heavy deed!<br>
It had been so with us, had we been there:<br>
His liberty is full of threats to all;<br>
To you yourself, to us, to every one.<br>
Alas, how shall this bloody deed be answer'd?<br>
It will be laid to us, whose providence<br>
Should have kept short, restrain'd and out of haunt,<br>
This mad young man: but so much was our love,<br>
We would not understand what was most fit;<br>
But, like the owner of a foul disease,<br>
To keep it from divulging, let it feed<br>
Even on the pith of Life. Where is he gone?<br>
<br>
QUEEN GERTRUDE <br>
To draw apart the body he hath kill'd:<br>
O'er whom his very madness, like some ore<br>
Among a mineral of metals base,<br>
Shows itself pure; he weeps for what is done.<br>
<br>
KING CLAUDIUS <br>
O Gertrude, come away!<br>
The sun no sooner shall the mountains touch,<br>
But we will ship him hence: and this vile deed<br>
We must, with all our majesty and skill,<br>
Both countenance and excuse. Ho, Guildenstern!<br>
<br>
<i>Re-enter ROSENCRANTZ and GUILDENSTERN</i><br>
<br>
Friends both, go join you with some further aid:<br>
Hamlet in madness hath Polonius slain,<br>
And from his mother's closet hath he dragg'd him:<br>
Go seek him out; speak fair, and bring the body<br>
Into the chapel. I pray you, haste in this.<br>
<br>
<i>Exeunt ROSENCRANTZ and GUILDENSTERN</i><br>
<br>
Come, Gertrude, we'll call up our wisest friends;<br>
And let them know, both what we mean to do,<br>
And what's untimely done. O, come away!<br>
My soul is full of discord and dismay.<br>
<br>
<i>Exeunt</i>
