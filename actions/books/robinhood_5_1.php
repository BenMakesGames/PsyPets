<p>And now we shall hear anon of other troubles than those
that beset gentle lovers, and how Robin Hood helped a good stout knight that
was in sore need of aid.  So listen to what follows.</p>

<h4 class="centered">Robin Hood aideth a Sorrowful Knight</h4>

<p>So passed the gentle springtime away in budding beauty; its silver showers and 
sunshine, its green meadows and its flowers. So, likewise, passed 
the summer with its yellow sunlight, its quivering heat and deep, 
bosky foliage, its long twilights and its mellow nights, through
which the frogs croaked and fairy folk were said to be out on the
hillsides. All this had passed and the time of fall had come, bringing
with it its own pleasures and joyousness, for now, when the
harvest was gathered home, merry bands of gleaners roamed the
country about, singing along the roads in the daytime, and sleeping
beneath the hedgerows and the hay-ricks at night. Now the
hips burned red in the tangled thickets and the haws waxed black
in the hedgerows, the stubble lay all crisp and naked to the sky,
and the green leaves were fast turning russet and brown. Also, at
this merry season, good things of the year are gathered in in great
store. Brown ale lies ripening in the cellar, hams and bacon hang
in the smoke-shed, and crabs are stowed away in the straw for
roasting in the winter time, when the north wind piles the snow
in drifts around the gables and the fire crackles warm upon the
hearth.</p> 

<p>So passed the seasons then, so they pass now, and so they will
pass in time to come, whilst we come and go like leaves of the
tree that fall and are soon forgotten.</p>

<p>Quoth Robin Hood, snuffing the air, "Here is a fair day, Little
John, and one that we can ill waste in idleness. Choose such men
as thou dost need, and go thou east while I will wend to the west,
and see that each of us bringeth back some goodly guest to dine
this day beneath the greenwood tree."</p>

<p>"Marry," cried Little John, clapping his palms together for
joy, "thy bidding fitteth my liking like haft to blade. I'll bring thee 
back a guest this day, or come not back mine own self."</p>

<p>Then they each chose such of the band as they wished, and so 
went forth by different paths from the forest.</p>

<p>Now, you and I cannot go two ways at the same time whilst 
we join in these merry doings; so we will e'en let Little John 
follow his own path while we tuck up our skirts and trudge after 
Robin Hood. And here is good company, too; Robin Hood, Will 
Scarlet, Allan a Dale, Will Scathelock, Midge, the Miller's son, 
and others. A score or more of stout fellows had abided in the 
forest, with Friar Tuck, to make ready for the home-coming, 
but all the rest were gone either with Robin Hood or Little John.</p>

<p>They travelled onward, Robin following his fancy and the others 
following Robin. Now they wended their way through an open 
dale with cottage and farm lying therein, and now again they entered 
woodlands once more. Passing by fair Mansfield Town, with 
its towers and battlements and spires all smiling in the sun, they 
came at last out of the forest lands. Onward they journeyed, 
through highway and byway, through villages where good wives 
and merry lasses peeped through the casements at the fine show 
of young men, until at last they came over beyond Alverton in 
Derbyshire. By this time high noontide had come, yet they had 
met no guest such as was worth their while to take back to Sherwood; 
so, coming at last to a certain spot where a shrine stood at 
the crossing of two roads, Robin called upon them to stop, for 
here on either side was shelter of high hedgerows, behind which 
was good hiding, whence they could watch the roads at their ease, 
whilst they ate their midday meal. Quoth merry Robin, "Here, 
methinks, is good lodging, where peaceful folk, such as we be, can 
eat in quietness, therefore we will rest here, and see what may, 
perchance, fall into our luck-pot." So they crossed a stile and came 
behind a hedgerow where the mellow sunlight was bright and 
warm, and where the grass was soft, and there sat them down. 
Then each man drew from the pouch that hung beside him that 
which he had brought to eat, for a merry walk such as this had 
been sharpens the appetite till it is as keen as a March wind. So 
no more words were spoken, but each man saved his teeth for 
better use munching at brown crust and cold meat right lustily.</p>

<p>In front of them, one of the high-roads crawled up the steep 
hill and then dipped suddenly over its crest, sharp-cut with 
hedgerow and shaggy grass against the sky. Over the top of the 
windy hill peeped the eaves of a few houses of the village that 
fell back into the valley behind; there, also, showed the top of a 
windmill, the sails slowly rising and dipping from behind the 
hill against the clear blue sky, as the light wind moved them with 
creaking and labored swing.</p>

<p>So the yeomen lay behind the hedge and finished their midday 
meal; but still the time slipped along, and no one came. At last, 
a man came slowly riding over the hill, and down the stony road 
toward the spot where Robin and his band lay hidden. He was 
a good stout knight, but sorrowful of face and downcast of mien. 
His clothes were plain and rich, but no chain of gold, such as 
folk of his stand in life wore at most times, hung around his neck, 
and no jewel was about him; yet no one could mistake him for 
aught but one of proud and noble blood. His head was bowed upon 
his breast and his hands drooped limp on either side; and so he 
came slowly riding, as though sunk in sad thoughts, whilst even 
his good horse, the reins loose upon his neck, walked with hanging
head, as though he shared his master's grief.</p>

<p>Quoth Robin Hood, "Yon is verily a sorry-looking gallant, and 
doth seem to have donned ill-content with his jerkin this morning; 
nevertheless, I will out and talk with him, for there may be some 
pickings here for a hungry daw. Methinks his dress is rich, though 
he himself is so downcast. Bide ye here till I look into this matter." 
So saying, he arose and left them, crossed the road to the shrine, 
and there stood, waiting for the sorrowful Knight to come near 
him. So, presently, when the Knight came riding slowly along, 
jolly Robin stepped forward and laid his hand upon the bridle 
rein. "Hold, Sir Knight," quoth he. "I prythee tarry for a short 
time, for I have a few words to say to thee."</p>

<p>"What art thou, friend, who dost stop a traveller in this manner 
upon his most gracious Majesty's highway?" said the Knight.</p>

<p>"Marry," quoth Robin, "that is a question hard to answer. One 
man calleth me kind, another calleth me cruel; this one calleth me 
good, honest fellow, and that one vile thief. Truly, the world hath 
as many eyes to look upon a man withal as there are spots on a 
toad; so, with what pair of eyes thou regardest me lieth entirely 
with thine own self. My name is Robin Hood."</p>

<p>"Truly, good Robin," said the Knight, a smile twitching at the 
comers of his mouth, "thou hast a quaint conceit. As for the pair 
of eyes with which I regard thee, I would say that they are as 
favorable as may be, for I hear much good of thee and little ill. 
What is thy will of me?"</p>

<p>"Now, I make my vow, Sir Knight," quoth Robin, "thou hast 
surely learned thy wisdom of good Gaffer Swanthold, for he sayeth, 
'Fair words are as easy spoke as foul, and bring good will in the 
stead of blows.' Now I will show thee the truth of this saying; 
for, if them wilt go with me this day to Sherwood Forest, I will give 
thee as merry a feast as ever thou hadst in all thy life."</p>

<p>"Thou art indeed kind," said the Knight, "but methinks thou 
wilt find me but an ill-seeming and sorrowful guest. Thou hadst 
best let me pass on my way in peace."</p>

<p>"Nay," quoth Robin, "thou mightst go thine own way but for 
one thing, and that I will tell thee. We keep an inn, as it were, in 
the very depths of Sherwood, but so far from high-roads and beaten 
paths that guests do not often come nigh us; so I and my friends 
set off merrily and seek them when we grow dull of ourselves. 
Thus the matter stands, Sir Knight; yet I will furthermore tell 
thee that we count upon our guests paying a reckoning."</p>

<p>"I take thy meaning, friend," said the Knight, gravely, "but I 
am not thy man, for I have no money by me."</p>

<p>"Is it sooth?" said Robin, looking at the Knight keenly. "I can 
scarce choose but believe thee; yet, Sir Knight, there be those of 
thy order whose word is not to be trusted as much as they would 
have others believe. Thou wilt think no ill if I look for myself in 
this matter." Then, still holding the horse by the bridle rein, he 
put his fingers to his lips and blew a shrill whistle, whereupon 
fourscore yeomen came leaping over the stile, and ran to where 
the Knight and Robin stood. "These," said Robin, looking upon 
them proudly, "are some of my merry men. They share and share 
alike with me all joys and troubles, gains and losses. Sir Knight, I 
prythee tell me what money thou hast about thee."</p>

<p>For a time the Knight said not a word, but a slow red arose 
into his cheeks; at last he looked Robin in the face and said: "I 
know not why I should be ashamed, for it should be no shame to 
me, but, friend, I tell thee the truth, when I say that in my purse 
are ten shillings, and that that is every groat that Sir Richard 
of the Lea hath in all the wide world."</p>

<p>When Sir Richard ended a silence fell, until at last Robin said: 
"And dost thou pledge me thy knightly word that this is all thou 
hast with thee?"</p>

<p>"Yea," answered Sir Richard, "I do pledge thee my most solemn 
word, as a true knight, that it is all the money I have in the world. 
Nay, here is my purse, ye may find for yourselves the truth of 
what I say." And he held his purse out to Robin.</p>

<p>"Put up thy purse, Sir Richard," quoth Robin. "Far be it from 
me to doubt the word of so gentle a knight. The proud I strive to 
bring low, but those that walk in sorrow I would aid if I could. 
Come, Sir Richard, cheer up thy heart and go with us into the 
greenwood. Even I may perchance aid thee, for thou surely knowest
how the good Athelstane was saved by the little blind mole 
that digged a trench over which he that sought the king's life 
stumbled."</p>

<p>"Truly, friend," said Sir Richard, "methinks thou meanest 
kindness in thine own way; nevertheless my troubles are such that 
it is not likely that thou canst cure them. But I will go with thee 
this day into Sherwood." Hereupon he turned his horse's head, 
and they all wended their way to the woodlands, Robin walking 
on one side of the Knight and Will Scarlet on the other, whilst 
the rest of the band trudged behind.</p>

<p>After they had travelled thus for a time Robin Hood spake. 
"Sir Knight," said he, "I would not trouble thee with idle questions,
but dost thou find it in thy heart to tell me thy sorrows?"</p>

<p>"Truly, Robin," quoth the Knight, "I see no reason why I 
should not do so. Thus it is: My castle and my lands are in pawn 
for a debt that I owe. Three days hence the money must be paid 
or else all mine estate is lost forever, for then it falls into the 
hands of the Priory of Emmet, and what they swallow they never 
give forth again."</p>

<p>Quoth Robin, "I understand not why those of thy kind live 
in such a manner that all their wealth passeth from them like 
snow beneath the springtide sun."</p>

<p>"Thou wrongest me, Robin," said the Knight, "for listen: I have 
a son but twenty winters old, nevertheless he has won his spurs as 
knight. Last year, on a certain evil day, the jousts were held at 
Chester, and thither my son went, as did I and my lady wife. I wot 
it was a proud time for us, for he unhorsed each knight that he 
tilted against. At last he ran a course with a certain great knight, 
Sir Walter of Lancaster, yet, though my son was so youthful, he 
kept his seat, albeit both spears were shivered to the haft; but it 
happened that a splinter of my boy's lance ran through the visor 
of Sir Walter's helmet, and pierced through his eye into his brain, 
so that he died ere his esquire could unlace his helm. Now, Robin, 
Sir Walter had great friends at court, therefore his kinsmen stirred 
up things against my son so that, to save him from prison, I 
had to pay a ransom of six hundred pounds in gold. All might 
have gone well even yet, only that, by ins and outs and crookedness
of laws, I was shorn like a sheep that is clipped to the quick. 
So it came that I had to pawn my lands to the Priory of Emmet 
for more money, and a hard bargain they drove with me in my 
hour of need. Yet I would have thee understand I grieve so for 
my lands only because of my dear lady wife."</p>

<p>"But where is thy son now?" asked Robin, who had listened 
closely to all the Knight had said.</p>

<p>"In Palestine," said Sir Richard, "battling like a brave Christian 
soldier for the cross and the holy sepulchre. Truly, England was 
an ill place for him because of Sir Walter's death, and the hate of 
the Lancastrian's kinsmen."</p>

<p>"Truly," said Robin, much moved, "thine is a hard lot. But 
tell me, what is owing to Emmet for thine estates?"</p>

<p>"Only four hundred pounds," said Sir Richard.</p>

<p>At this Robin smote his thigh in anger. "O the blood-suckers!" 
cried he. "A noble estate to be forfeit for four hundred pounds! 
But what will befall thee if thou dost lose thy lands, Sir Richard?"</p>

<p>"It is not mine own lot that doth trouble me in that case," 
said the Knight, "but my dear lady's; for should I lose my land 
she will have to betake herself to some kinsman and there abide 
in charity, which, methinks, would break her proud heart. As for 
me, I will over the salt sea, and so to Palestine to join my son in 
fight for the holy sepulchre."</p>

<p>Then up spake Will Scarlet. "But hast thou no friend that will 
help thee in thy dire need?"</p>

<p>"Never a man," said Sir Richard. "Whilst I was rich enow at 
home, and had friends, they blew great boasts of how they loved 
me. But when the oak falls in the forest the swine run from beneath 
it lest they should be smitten down also. So my friends 
have left me; for not only am I poor but I have great enemies."</p>

<p>Then Robin said, "Thou sayst thou hast no friends, Sir Richard. 
I make no boast, but many have found Robin Hood a friend in 
their troubles. Cheer up, Sir Knight, I may help thee yet."</p>

<p>The Knight shook his head with a faint smile, but for all that 
Robin's words made him more blithe of heart, for in truth hope, 
be it never so faint, bringeth a gleam into darkness, like a little 
rushlight that costeth but a groat.</p>

<p>The day was well-nigh gone when they came near to the greenwood 
tree. Even at a distance they saw by the number of men that 
Little John had come back with some guest, but when they came 
near enough, who should they find but the Lord Bishop of Hereford. 
The good Bishop was in a fine stew, I wot. Up and down he 
walked beneath the tree like a fox caught in a hencoop. Behind 
him were three black friars standing close together in a frightened 
group, like three black sheep in a tempest. Hitched to the branches 
of the trees close at hand were six horses, one of them a barb 
with gay trappings upon which the Bishop was wont to ride, and 
the others laden with packs of divers shapes and kinds, one of 
which made Robin's eyes glisten, for it was a box not over large, 
but heavily bound with bands and ribs of iron.</p>

<p>When the Bishop saw Robin and those with him come into the 
open he made as though he would have run toward the yeoman, 
but the fellow that guarded the Bishop and the three friars thrust 
his quarterstaff in front, so that his lordship was fain to stand 
back, though with frowning brow and angry speech.</p>

<p>"Stay, my Lord Bishop," cried jolly Robin, in a loud voice, when 
he saw what had passed; "I will come to thee with all speed, for 
I would rather see thee than any man in merry England." So 
saying, he quickened his steps, and soon came to where the Bishop 
stood fuming.</p>

<p>"How now," quoth the Bishop in a loud and angry voice, when 
Robin had so come to him, "is this the way that thou and thy 
band treat one so high in the church as I am? I and these brethren 
were passing peacefully along the high-road with our packhorses, 
and a halfscore of men to guard them, when up comes a great 
strapping fellow full seven feet high, with fourscore or more men 
back of him, and calls upon me to stop me, the Lord Bishop of 
Hereford, mark thou! Whereupon my armed guards beshrew 
them for cowards! straight ran away. But look ye; not only did 
this fellow stop me, but he threatened me, saying that Robin 
Hood would strip me as bare as a winter hedge. Then, beside all 
this, he called me such vile names as 'fat priest,' 'man-eating 
bishop,' 'money-gorging usurer,' and what not, as though I were 
no more than a strolling beggar or tinker. Moreover, when I came 
here I found a great fat man, a mock priest, that slapped me upon 
the shoulder as though I, God wot, were a pot-house fellow."</p>

<p>"Marry, come up with a wanion!" cried Friar Tuck, bustling 
forward and thrusting himself in front of the Bishop. "Marry 
come up, I say!" and he snapped his fingers under the Bishop's 
nose, whereat the other started back as though the snap were a 
clap of thunder. " 'Mock priest!' thou callest me, forsooth! Look ye 
now, Bishop, I wot I am as holy a man as thou art, and might have 
been a bishop mine own self, had I not been born under a hedge. 
I am as learned, too, as thou art, albeit I could never master that 
vile Latin, my tongue being only shaped for good stout English, 
yet I tell thee, I can say my 'Paters' and 'Aves' with no more a 
slip o' the tongue than thou, thou fat man!"</p>

<p>At this the Bishop glared upon the stout Friar like an angry 
cat, whilst even Sir Richard laughed; only Robin kept a grave 
face. "Stand back, Tuck," said he, "thou shouldst not beard his 
lordship's reverence in this wise. Alas, my lord, that thou hast 
been so ill-treated by my band! I tell thee truly that we greatly 
reverence thy cloth. Little John, stand forth straightway."</p>

<p>At these words Little John came forward, twisting his face into 
a whimsical look, as though he would say, "Ha' mercy upon me, 
good master." Then Robin turned to the Bishop of Hereford and 
said: "Was this the man who spake so boldly to your lordship?"</p>

<p>"Ay, truly it was the same," said the Bishop; "a naughty fellow, I wot."</p>

<p>"And didst thou, Little John," said Robin, in a sad voice, "call 
his lordship a fat priest?"</p>

<p>"Ay," said Little John, sorrowfully.</p>

<p>"And a man-eating bishop?"</p>

<p>"Ay," said Little John, more sorrowfully than before.</p>

<p>"And a money-gorging usurer?"</p>

<p>"Ay," said Little John, in so sorrowful a voice that it might 
have drawn tears from the Dragon of Wentley.</p>

<p>"Alas, that these things should be!" said jolly Robin, turning 
to the Bishop, "for I have ever found Little John a truthful man."</p>

<p>At this a roar of laughter went up, whereat the blood rushed 
into the Bishop's face till it was cherry red from crown to chin; 
but he said nothing, and only swallowed his words, though they 
well-nigh choked him.</p>

<p>"Nay, my Lord Bishop," said Robin, "we are rough fellows, but 
I trust not such ill men as thou thinkest, after all. There is not a 
man here that would harm a hair of thy reverence's head. I know 
thou art galled by our jesting, but we are all equal here in the 
greenwood, for there are no bishops nor barons nor earls among 
us, but only men, so thou must share our life with us whilst thou 
dost abide here. Come, busk ye, my merry men, and get the feast 
ready. Meantime we will show our guests our woodland sports."</p>

<p>So, whilst some went to kindle the fires for roasting meats, 
others ran leaping to get their cudgels and long bows. Then Robin 
brought forward Sir Richard o' the Lea. "My Lord Bishop," said 
he, "here is another guest that we have with us this day. I wish 
that thou mightst know him better, for I and all my men will 
strive to honor you both at this merrymaking."</p>

<p>"Sir Richard," said the Bishop, in a reproachful tone, "methinks
thou and I are companions and fellow sufferers in this den 
of" He was about to say "thieves," but he stopped suddenly 
and looked askance at Robin Hood.</p>

<p>"Speak out, Bishop," quoth Robin, laughing. "We of Sherwood 
check not an easy flow of words. 'Den of thieves' thou wast about 
to say."</p>

<p>Quoth the Bishop, "Mayhap that was what I meant to say, 
Sir Richard, but this I will say, that I saw thee just now laugh at 
the scurrilous jests of these fellows. It would have been more becoming 
of thee, methinks, to have checked them with frowns instead 
of spurring them on by laughter."</p>

<p>"I meant no harm to thee," said Sir Richard; "but a merry jest 
is a merry jest, and I may truly say I would have laughed at it had 
it been against mine own self."</p>

<p>But now Robin Hood called upon certain ones of his band who 
spread soft moss upon the ground and laid deer skins thereon. 
Then Robin bade his guests be seated, and so they all three sat 
down, some of the chief men, such as Little John, Will Scarlet, 
Allan a Dale, and others, stretching themselves upon the ground 
near by. Then a garland was set up at the far end of the glade, and 
thereat the bowmen shot, and such shooting was done that day as 
it would have made one's heart leap to see. And all the while 
Robin talked so quaintly to the Bishop and the Knight that, the 
one forgetting his vexation and the other his troubles, they 
both laughed aloud again and again.</p>

<p>Ten men shot three rounds of arrows each, and although the 
garland was but three palms' breadth wide, and was full sevenscore 
yards distant, only two arrows went without the ring. "By 
Our Lady, good friend," said the Bishop to Robin, "never did I 
see such shooting in all my life as these men of thine do. But I have 
heard so oft of thy skill, canst thou not show us a touch of it?"</p>

<p>"Why," quoth Robin, "the light groweth somewhat dim, and 
things begin to glimmer, ne'ertheless I will try what I can do." 
So saying he arose from where he sat, then, drawing his dagger, 
he cut a hazel wand a little greater in girth than a man's thumb, 
and peeling the bark therefrom, he walked with measured steps 
fourscore yards distance. There he thrust the staff into the ground 
and came back to where the others sat, and Allan a Dale handed 
him his good stout yew bow, which Robin forthwith strung. 
Then emptying his quiver upon the ground, he searched among 
the arrows carefully till he had chosen one to his liking. Having 
so done, he nocked the arrow and stood in position, and all around 
was so hushed that you might have heard the falling of a leaf. 
Then he drew the string quickly to his ear, and straightened his 
bow arm, and ere you could draw a breath loosed the string with a 
twang. So swift flew the arrow that the eye could not follow it, 
but a great shout went up from the yeomen when it had sped, and 
Will Scathelock ran leaping and brought the wand, and lo, the 
arrow was sticking in the wood which it had cleft. Then all the 
yeomen shouted again till those about the fires came running, for 
they were proud of their master's skill, which none could hope to 
match.</p>

<p>But meantime Robin had set him down again between his 
guests; then, without giving them time for word of praise, he called 
upon those of his band who were the most deft at quarterstaff. So 
they sat and watched the game till the shades of evening fell, 
and there was no light in which to give stroke or parry.</p>

<p>Then Allan a Dale came forth and tuned his harp, and all was 
hushed around, and he sang in his wondrous voice songs of love, 
of war, of glory, and of sadness, and all listened without a movement 
or a sound. So Allan sang till the great round silver moon 
gleamed with its clear white light amid the upper tangle of the 
mazy branches of the trees.</p>

<p>At last two fellows came to say that the feast was ready spread, 
so Robin, leading his guests with either hand, brought them to 
where great smoking dishes, that sent savory smells far and near, 
stood along the white linen cloth spread on the grass. All around 
was a glare of torches that lit everything up with a red light. Then, 
straightway sitting down, all fell to with noise and hubbub, the 
rattling of platters blending with the sound of loud talking and 
laughter. A long time the feast lasted, but at last all was over, 
and the bright wine and humming ale passed briskly. Then Robin 
Hood called aloud for silence, and all was hushed till he spoke.</p>

<p>"I have a story to tell you all, so listen to what I have to say," 
quoth he; whereupon, without more ado, he told them all about 
Sir Richard, and how his lands were in pawn. But, as he 
went on, the Bishop's face, that had erst been smiling and ruddy 
with merriment, waxed serious, and he put aside the horn of wine 
he held in his hand, for he knew the story of Sir Richard, and his 
heart sank within him with grim forebodings. Then, when Robin 
Hood had done, he turned to the Bishop of Hereford. "Now, my 
Lord Bishop," said he, "dost thou not think this is ill done of 
any one, much more of a churchman, who should live in humbleness
and charity?"</p>

<p>To this the Bishop answered not a word, but looked upon the 
ground with moody eyes.</p>

<p>Quoth Robin, "Now, thou art the richest bishop in all England. 
Canst thou not help this needy brother?" But still the Bishop 
answered not a word.</p>

<p>Then Robin turned to Little John, and quoth he, "Go thou 
and Will Stutely and bring forth those five pack-horses yonder." 
Whereupon the two yeomen did as they were bidden, those about 
the cloth making room on the green, where the light was brightest, 
for the five horses which Little John and Will Stutely presently
led forward.</p>

<p>"Who hath the score of the goods?" asked Robin Hood, looking 
at the Black Friars.</p>

<p>Then up spake the smallest of all, in a trembling voice, an old 
man he was, with a gentle, wrinkled face. "That have I, but, I 
pray thee, harm me not."</p>

<p>"Nay," quoth Robin, "I have never harmed harmless man yet, 
but give it to me, good father." So the old man did as he was 
bidden, and handed Robin the tablet on which was marked down 
the account of the various packages upon the horses. This 
Robin handed to Will Scarlet, bidding him to read the same. So 
Will Scarlet, lifting his voice that all might hear, began:</p>

<p>"Three bales of silk to Quentin, the mercer at Ancaster."</p>

<p>"That we touch not," quoth Robin, "for this Quentin is an honest 
fellow, who hath risen by his own thrift." So the bales of silk 
were laid aside without being opened.</p>

<p>"One bale of silk velvet for the Abbey of Beaumont."</p>

<p>"What do these priests want of silk velvet?" quoth Robin. 
"Nevertheless, though they need it not, I will not take all from 
them. Measure it off into three lots, one to be sold for charity, 
one for us, and one for the abbey." So this, too, was done as 
Robin Hood bade.</p>

<p>"Twoscore of great wax candles for the Chapel of Saint 
Thomas."</p>

<p>"That belongeth fairly to the chapel," quoth Robin, "so lay it to 
one side. Far be it from us to take from the blessed Saint Thomas 
that which belongeth to him." So this, also, was done according 
to Robin's bidding, and the candles were laid to one side, along 
with honest Quentin's unopened bales of silk. So the list was 
gone through with, and the goods adjudged according to what 
Robin thought most fit. Some things were laid aside untouched, 
and many were opened and divided into three equal parts, for 
charity, for themselves, and for the owners. And now all the 
ground in the torchlight was covered over with silks and velvets 
and cloths of gold and cases of rich wines, and so they came to 
the last line upon the tablet,&mdash;</p>

<p>"A box belonging to the Lord Bishop of Hereford."</p>

<p>At these words the Bishop shook as with a chill, and the box 
was set upon the ground.</p>

<p>"My Lord Bishop, hast thou the key of this box?" asked Robin.</p>

<p>The Bishop shook his head.</p>

<p>"Go, Will Scarlet," said Robin, "thou art the strongest man 
here bring a sword straightway, and cut this box open, if thou 
canst." Then up rose Will Scarlet and left them, coming back in 
a short time, bearing a great two-handed sword. Thrice he smote 
that strong, iron-bound box, and at the third blow it burst open 
and a great heap of gold came rolling forth, gleaming red in 
the light of the torches. At this sight a murmur went all around 
among the band, like the sound of the wind in distant trees; but 
no man came forward nor touched the money.</p>

<p>Quoth Robin, "Thou, Will Scarlet, thou, Allan a Dale, and 
thou, Little John, count it over."</p>

<p>A long time it took to count all the money, and when it had 
been duly scored up, Will Scarlet called out that there were fifteen 
hundred golden pounds in all. But in among the gold they 
found a paper, and this Will Scarlet read in a loud voice, and all 
heard that this money was the rental and fines and forfeits from 
certain estates belonging to the Bishopric of Hereford.</p>

<p>"My Lord Bishop," said Robin Hood, "I will not strip thee, 
as Little John said, like a winter hedge, for thou shalt take back 
one third of thy money. One third of it thou canst well spare to 
us for thy entertainment and that of thy train, for thou art very 
rich; one third of it thou canst better spare for charity, for Bishop, 
I hear that thou art a hard master to those beneath thee and a 
close hoarder of gains that thou couldst better and with more 
credit to thyself give to charity than spend upon thy own likings."</p>

<p>At this the Bishop looked up, but he could say never a word, 
yet he was thankful to keep some of his wealth.</p>

<p>Then Robin turned to Sir Richard of the Lea, and quoth he, 
"Now, Sir Richard, the church seemed like to despoil thee, therefore 
some of the overplus of church gains may well be used in 
aiding thee. Thou shalt take that five hundred pounds laid aside 
for people more in need than the Bishop is, and shalt pay thy 
debts to Emmet therewith."</p>

<p>Sir Richard looked at Robin until something arose in his eyes 
that made all the lights and the faces blur together. At last he 
said, "I thank thee, friend, from my heart, for what thou doest for 
me; yet, think not ill if I cannot take thy gift freely. But this I will 
do. I will take the money and pay my debts, and in a year and a 
day hence will return it safe either to thee or to the Lord Bishop 
of Hereford. For this I pledge my most solemn knightly word. I 
feel free to borrow, for I know no man that should be more bound 
to aid me than one so high in that church that hath driven such a 
hard bargain with me."</p>

<p>"Truly, Sir Knight," quoth Robin, "I do not understand those 
fine scruples that weigh with those of thy kind, but, nevertheless, 
it shall all be as thou dost wish. But thou hadst best bring the 
money to me at the end of the year, for mayhap I may make 
better use of it than the Bishop." Thereupon, turning to those 
near him, he gave his orders, and five hundred pounds were 
counted out and tied up in a leathern bag for Sir Richard. The 
rest of the treasure was divided, and part taken to the treasurehouse 
of the band, and part put by with the other things for the 
Bishop.</p>

Then Sir Richard arose. "I cannot stay later, good friends," 
said he, "for my lady will wax anxious if I come not home, so I 
crave leave to depart."</p>

Then Robin Hood and all his merry men arose, and Robin said, 
"We cannot let thee go hence unattended, Sir Richard."</p>

Then up spake Little John: "Good master, let me choose a score 
of stout fellows from the band, and let us arm ourselves in a 
seemly mannner, and so serve as retainers to Sir Richard till he 
can get others in our stead."</p>

"Thou hast spoken well, Little John, and it shall be done," 
said Robin.</p>

Then up spake Will Scarlet: "Let us give him a golden chain 
to hang about his neck, such as befits one of his blood, and also 
golden spurs to wear at his heels."</p>

Then Robin Hood said, "Thou hast spoken well, Will Scarlet, 
and it shall be done."</p>

<p>Then up spake Will Stutely: "Let us give him yon bale of rich 
velvet and yon roll of cloth of gold to take home to his noble lady 
wife as a present from Robin Hood and his merry men all."</p>

<p>At this all clapped their hands for joy, and Robin said: "Thou 
hast well spoken, Will Stutely, and it shall be done."</p>

<p>Then Sir Richard o' the Lea looked all around and strove to 
speak, but could scarcely do so for the feelings that choked him. 

<p>At last he said in a husky, trembling voice, "Ye shall all see, good 
friends, that Sir Richard o' the Lea will ever remember your kindness 
this day. And if ye be at any time in dire need or trouble, 
come to me and my lady, and the walls of Castle Lea shall be 
battered down ere harm shall befall you. I" He could say nothing 
further, but turned hastily away.</p>

<p>But now Little John and nineteen stout fellows, whom he had 
chosen for his band, came forth all ready for the journey. Each 
man wore upon his breast a coat of linked mail, and on his head 
a cap of steel, and at his side a good stout sword. A gallant show 
they made as they stood all in a row. Then Robin came and threw 
a chain of gold about Sir Richard's neck, and Will Scarlet knelt 
and buckled the golden spurs upon his heel; and now Little 
John led forward Sir Richard's horse, and the Knight mounted. 
He looked down at Robin for a little time, then of a sudden 
stooped and kissed his cheek. All the forest glades rang with the 
shout that went up as the Knight and the yeomen marched off 
through the woodland with glare of torches and gleam of steel, 
and so were gone.</p>

<p>Then up spake the Bishop of Hereford in a mournful voice: "I, 
too, must be jogging, good fellow, for the night waxes late."</p>

<p>But Robin laid his hand upon the Bishop's arm and stayed 
him. "Be not so hasty, Lord Bishop," said he. "Three days hence 
Sir Richard must pay his debts to Emmet; until that time thou 
must be content to abide with me lest thou breed trouble for the 
Knight. I promise thee that thou shalt have great sport, for I know 
that thou art fond of hunting the dun deer. Lay by thy mantle of 
melancholy, and strive to lead a joyous yeoman life for three stout 
days. I promise thee thou shalt be sorry to go when the time has 
come."</p>

<p>So the Bishop and his train abided with Robin for three days, 
and much sport his lordship had in that time, so that, as Robin 
had said, when the time had come for him to go he was sorry to 
leave the greenwood. At the end of three days Robin set him 
free, and sent him forth from the forest with a guard of yeomen 
to keep freebooters from taking what was left of the packs and 
bundles.</p>

<p>But, as the Bishop rode away, he vowed within himself that he 
would sometime make Robin rue the day that he stopped him in 
Sherwood.</p>
