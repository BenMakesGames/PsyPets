SCENE III. Another room in the castle.<br>
<br>
<i>Enter KING CLAUDIUS, attended</i><br>
KING CLAUDIUS <br>
I have sent to seek him, and to find the body.<br>
How dangerous is it that this man goes loose!<br>
Yet must not we put the strong law on him:<br>
He's loved of the distracted multitude,<br>
Who like not in their judgment, but their eyes;<br>
And where tis so, the offender's scourge is weigh'd,<br>
But never the offence. To bear all smooth and even,<br>
This sudden sending him away must seem<br>
Deliberate pause: diseases desperate grown<br>
By desperate appliance are relieved,<br>
Or not at all.<br>
<br>
<i>Enter ROSENCRANTZ</i><br>
<br>
How now! what hath befall'n?<br>
<br>
ROSENCRANTZ <br>
Where the dead body is bestow'd, my lord,<br>
We cannot get from him.<br>
<br>
KING CLAUDIUS <br>
But where is he?<br>
<br>
ROSENCRANTZ <br>
Without, my lord; guarded, to know your pleasure.<br>
<br>
KING CLAUDIUS <br>
Bring him before us.<br>
<br>
ROSENCRANTZ <br>
Ho, Guildenstern! bring in my lord.<br>
<br>
<i>Enter HAMLET and GUILDENSTERN</i><br>
<br>
KING CLAUDIUS <br>
Now, Hamlet, where's Polonius?<br>
<br>
HAMLET <br>
At supper.<br>
<br>
KING CLAUDIUS <br>
At supper! where?<br>
<br>
HAMLET <br>
Not where he eats, but where he is eaten: a certain<br>
convocation of politic worms are e'en at him. Your<br>
worm is your only emperor for diet: we fat all<br>
creatures else to fat us, and we fat ourselves for<br>
maggots: your fat king and your lean beggar is but<br>
variable service, two dishes, but to one table:<br>
that's the end.<br>
<br>
KING CLAUDIUS <br>
Alas, alas!<br>
<br>
HAMLET <br>
A man may fish with the worm that hath eat of a<br>
king, and cat of the fish that hath fed of that worm.<br>
<br>
KING CLAUDIUS <br>
What dost you mean by this?<br>
<br>
HAMLET <br>
Nothing but to show you how a king may go a<br>
progress through the guts of a beggar.<br>
<br>
KING CLAUDIUS <br>
Where is Polonius?<br>
<br>
HAMLET <br>
In heaven; send hither to see: if your messenger<br>
find him not there, seek him i' the other place<br>
yourself. But indeed, if you find him not within<br>
this month, you shall nose him as you go up the<br>
stairs into the lobby.<br>
<br>
KING CLAUDIUS <br>
Go seek him there.<br>
<br>
<i>To some Attendants</i><br>
<br>
HAMLET <br>
He will stay till ye come.<br>
<br>
<i>Exeunt Attendants</i><br>
<br>
KING CLAUDIUS <br>
Hamlet, this deed, for thine especial safety,--<br>
Which we do tender, as we dearly grieve<br>
For that which thou hast done,--must send thee hence<br>
With fiery quickness: therefore prepare thyself;<br>
The bark is ready, and the wind at help,<br>
The associates tend, and every thing is bent<br>
For England.<br>
<br>
HAMLET <br>
For England!<br>
<br>
KING CLAUDIUS <br>
Ay, Hamlet.<br>
<br>
HAMLET <br>
Good.<br>
<br>
KING CLAUDIUS <br>
So is it, if thou knew'st our purposes.<br>
<br>
HAMLET <br>
I see a cherub that sees them. But, come; for<br>
England! Farewell, dear mother.<br>
<br>
KING CLAUDIUS <br>
Thy loving father, Hamlet.<br>
<br>
HAMLET <br>
My mother: father and mother is man and wife; man<br>
and wife is one flesh; and so, my mother. Come, for England!<br>
<br>
<i>Exit</i><br>
<br>
KING CLAUDIUS <br>
Follow him at foot; tempt him with speed aboard;<br>
Delay it not; I'll have him hence to-night:<br>
Away! for every thing is seal'd and done<br>
That else leans on the affair: pray you, make haste.<br>
<br>
<i>Exeunt ROSENCRANTZ and GUILDENSTERN</i><br>
<br>
And, England, if my love thou hold'st at aught--<br>
As my great power thereof may give thee sense,<br>
Since yet thy cicatrice looks raw and red<br>
After the Danish sword, and thy free awe<br>
Pays homage to us--thou mayst not coldly set<br>
Our sovereign process; which imports at full,<br>
By letters congruing to that effect,<br>
The present death of Hamlet. Do it, England;<br>
For like the hectic in my blood he rages,<br>
And thou must cure me: till I know 'tis done,<br>
Howe'er my haps, my joys were ne'er begun.<br>
<br>
<i>Exit</i><br>
