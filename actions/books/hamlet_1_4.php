SCENE IV. The platform.<br>
<br>
<i>Enter HAMLET, HORATIO, and MARCELLUS</i><br>
HAMLET <br>
The air bites shrewdly; it is very cold.<br>
<br>
HORATIO <br>
It is a nipping and an eager air.<br>
<br>
HAMLET <br>
What hour now?<br>
<br>
HORATIO <br>
I think it lacks of twelve.<br>
<br>
HAMLET <br>
No, it is struck.<br>
<br>
HORATIO <br>
Indeed? I heard it not: then it draws near the season<br>
Wherein the spirit held his wont to walk.<br>
<br>
<i>A flourish of trumpets, and ordnance shot off, within</i><br>
<br>
What does this mean, my lord?<br>
<br>
HAMLET <br>
The king doth wake to-night and takes his rouse,<br>
Keeps wassail, and the swaggering up-spring reels;<br>
And, as he drains his draughts of Rhenish down,<br>
The kettle-drum and trumpet thus bray out<br>
The triumph of his pledge.<br>
<br>
HORATIO <br>
Is it a custom?<br>
<br>
HAMLET <br>
Ay, marry, is't:<br>
But to my mind, though I am native here<br>
And to the manner born, it is a custom<br>
More honour'd in the breach than the observance.<br>
This heavy-headed revel east and west<br>
Makes us traduced and tax'd of other nations:<br>
They clepe us drunkards, and with swinish phrase<br>
Soil our addition; and indeed it takes<br>
From our achievements, though perform'd at height,<br>
The pith and marrow of our attribute.<br>
So, oft it chances in particular men,<br>
That for some vicious mole of nature in them,<br>
As, in their birth--wherein they are not guilty,<br>
Since nature cannot choose his origin--<br>
By the o'ergrowth of some complexion,<br>
Oft breaking down the pales and forts of reason,<br>
Or by some habit that too much o'er-leavens<br>
The form of plausive manners, that these men,<br>
Carrying, I say, the stamp of one defect,<br>
Being nature's livery, or fortune's star,--<br>
Their virtues else--be they as pure as grace,<br>
As infinite as man may undergo--<br>
Shall in the general censure take corruption<br>
From that particular fault: the dram of eale<br>
Doth all the noble substance of a doubt<br>
To his own scandal.<br>
<br>
HORATIO <br>
Look, my lord, it comes!<br>
<br>
<i>Enter Ghost</i><br>
<br>
HAMLET <br>
Angels and ministers of grace defend us!<br>
Be thou a spirit of health or goblin damn'd,<br>
Bring with thee airs from heaven or blasts from hell,<br>
Be thy intents wicked or charitable,<br>
Thou comest in such a questionable shape<br>
That I will speak to thee: I'll call thee Hamlet,<br>
King, father, royal Dane: O, answer me!<br>
Let me not burst in ignorance; but tell<br>
Why thy canonized bones, hearsed in death,<br>
Have burst their cerements; why the sepulchre,<br>
Wherein we saw thee quietly inurn'd,<br>
Hath oped his ponderous and marble jaws,<br>
To cast thee up again. What may this mean,<br>
That thou, dead corse, again in complete steel<br>
Revisit'st thus the glimpses of the moon,<br>
Making night hideous; and we fools of nature<br>
So horridly to shake our disposition<br>
With thoughts beyond the reaches of our souls?<br>
Say, why is this? wherefore? what should we do?<br>
<br>
<i>Ghost beckons HAMLET</i><br>
<br>
HORATIO <br>
It beckons you to go away with it,<br>
As if it some impartment did desire<br>
To you alone.<br>
<br>
MARCELLUS <br>
Look, with what courteous action<br>
It waves you to a more removed ground:<br>
But do not go with it.<br>
<br>
HORATIO <br>
No, by no means.<br>
<br>
HAMLET <br>
It will not speak; then I will follow it.<br>
<br>
HORATIO <br>
Do not, my lord.<br>
<br>
HAMLET <br>
Why, what should be the fear?<br>
I do not set my life in a pin's fee;<br>
And for my soul, what can it do to that,<br>
Being a thing immortal as itself?<br>
It waves me forth again: I'll follow it.<br>
<br>
HORATIO <br>
What if it tempt you toward the flood, my lord,<br>
Or to the dreadful summit of the cliff<br>
That beetles o'er his base into the sea,<br>
And there assume some other horrible form,<br>
Which might deprive your sovereignty of reason<br>
And draw you into madness? think of it:<br>
The very place puts toys of desperation,<br>
Without more motive, into every brain<br>
That looks so many fathoms to the sea<br>
And hears it roar beneath.<br>
<br>
HAMLET <br>
It waves me still.<br>
Go on; I'll follow thee.<br>
<br>
MARCELLUS <br>
You shall not go, my lord.<br>
<br>
HAMLET <br>
Hold off your hands.<br>
<br>
HORATIO <br>
Be ruled; you shall not go.<br>
<br>
HAMLET <br>
My fate cries out,<br>
And makes each petty artery in this body<br>
As hardy as the Nemean lion's nerve.<br>
Still am I call'd. Unhand me, gentlemen.<br>
By heaven, I'll make a ghost of him that lets me!<br>
I say, away! Go on; I'll follow thee.<br>
<br>
<i>Exeunt Ghost and HAMLET</i><br>
<br>
HORATIO <br>
He waxes desperate with imagination.<br>
<br>
MARCELLUS <br>
Let's follow; 'tis not fit thus to obey him.<br>
<br>
HORATIO <br>
Have after. To what issue will this come?<br>
<br>
MARCELLUS <br>
Something is rotten in the state of Denmark.<br>
<br>
HORATIO <br>
Heaven will direct it.<br>
<br>
MARCELLUS <br>
Nay, let's follow him.<br>
<br>
<i>Exeunt</i><br>
