<h5>Wines and Beers</h5>
<p>Fermenting is a process that can take some time, but with the help of Aging Root, takes mere moments.</p>
<p>You can make wines, and other alcohols, from many different fruits and vegetables.</p>
<ul>
 <li>Any berry will yield a sweet wine.</li>
 <li>Rice can be used to make Sake.</li>
 <li>Potatoes, aged, yield Vodka.</li>
 <li>Grains, in combination with Hops, and other ingredients, make beers.</li>
</ul>
<p>Be careful to control the Yeast in your wines and Vodka, or you'll end up with Vinegar!</p>
<p>Following are a few select recipes:</p>
<p><u>Shōchū and Umeshu</u></p>
<p>Shōchū is a Japanese alcohol made by fermenting Yam!</p>
<p>Add Sugar and Apricot to Shōchū to get the sweeter Umeshu.</p>
<p><u>Witbier</u></p>
<p>Witbier - "white beer" - is made by fermenting either Wheat or Barley with a combination of Yeast, Hop, Coriander, and Orange.</p>
<h5>Soy Sauce</h5>
<p>Soy Sauce is nothing more than fermented Soy Beans.  Don't forget to add some kind of Sugar, however.</p>
<h5>Old-style Sodas</h5>
<p>Soda is fascinating to make: the carbonation is created by letting Yeast consume Sugar and "exhale" carbon dioxide in to your brew.</p>
<p>This is where Aging Root comes in, to speed the carbonation process.</p>
<p>A small amount of fermentation will occur, but it is nothing to worry about.  Sodas are a snack enjoyed safely by many children.</p>
<p>Ginger Beer is a classic soda recipe including Ginger and Sour Lime.</p>
<h5>Yogurt and Cheese</h5>
<p>Age some Milk (not Raw Milk, though) to get Yogurt.  Further aging will result in a nice Cheese.</p>
<h5>Other Uses</h5>
<p>Aging Root is a strange vegetable whose properities are still not fully understood, and people are still discovering new and useful applications all the time.</p>
<p>For example, one unusual use of Aging Root is to speed the growth of fungus on Logs, yielding Mushrooms (just be careful about Poisonous Mushrooms!)  It's also been suggested that Aging Root could be used to induce rusting in certain metals.</p>
<p>Keep experimenting, and you, too, may discover a new use for Aging Root!</p>
