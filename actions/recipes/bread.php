<h5>White Bread</h5>
<p>White bread is easy to make, once you know the recipe.  The trick is finding the ingredients you'll need.</p>
<ul>
 <li>
  <b>Flour</b><br />
  Flour is made by grinding Wheat, which is fairly easy to find in the wild, or your local Grocery Store.
 </li>
 <li>
  <b>Milk</b><br />
  Do not use Raw Milk!  You'll end up with Croissants!  If you do have Raw Milk, separate it into Milk and Cream and use the Milk.
 </li>
 <li>
  <b>Sugar</b><br />
  Sugar can be acquired, in quantities, from Sugar Beet, which itself is a fairly common plant.
 </li>
 <li>
  <b>Yeast</b><br />
  Yeast is the trickiest of the lot.  Fortunately, it can be grown from fairly common ingredients:  Potato and Sugar.
 </li>
</ul>
<p>Once you have the above four ingredients, mix them together and bake!  Optionally, add Butter or Shortening for flavor (and a slightly larger yield).</p>
<h5>Cornbread</h5>
<p>Cornbread uses a similar set of ingredients, but instead of Yeast we rely on Baking Powder.  Of course, you also need Cornmeal!  Finally, Egg is added for consistency.</p>
<ul>
 <li><b>Flour</b></li>
 <li><b>Milk</b><br />
  Again, don't use Raw Milk.
 </li>
 <li><b>Sugar</b></li>
 <li>
  <b>Baking Powder</b><br />
  Baking Powder is nothing more than Baking Soda and Cream of Tartar combined.  (Did you know that Cream of Tartar is a by-product of wine-making?  It collects around the edges of Barrels as wine ages.)
 </li>
 <li><b>Cornmeal</b><br />
  Grind together a couple Corn to get not only Cornmeal, but Grits and Cornstarch!
 </li>
 <li><b>Egg</b><br />
  Without Egg, the bread will not hold together, and you'll end up with a mess!  So don't forget it!
 </li>
</ul>
