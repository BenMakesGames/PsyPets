<h6>Equipment</h6>
<p>When you go out into the wild, the most important thing to have is equipment!  Here are some suggestions:</p>
<p><b>Binoculars</b></p>
<p>Binoculars will help you see a great distance, the uses for which should be obvious.  If you can't find a good pair of binoculars, you can make your own with a couple Plastics and Glass.  Alternatively, a Spyglass made of Tin and Glass, however these are less effective.</p>
<p><b>Compass</b></p>
<p>Under the night sky it should be pretty easy for you to determine which direction north is.  However, unless you have a lot of time, it can be a difficult thing to do during the day.  This is where a compass comes in to play.</p>
<p>Compasses are also fairly simple to build, requiring a bit of Iron, and a plastic casing (metal will not do, as it will interfere with the operation of your compass!)</p>
<p><b>Maps</b></p>
<p>If you have any local maps, you should be able to determine your location with relative ease.</p>
<h6>Food</h6>
<p>Fortunately food is relatively easy to come by in the wilderness.  If you run out of supplies, here are some simple recipes to try:</p>
<p><b>Berries</b></p>
<p>You can do a lot with berries!  Mash a few together to make Jelly, or combine with Aging Root for wine.  If you can also find Tea Leaves, Berry Tea can be very refreshing.</p>
<p><b>Coconut</b></p>
<p>Coconut is a great find in the wild!  Break it open to reveal the Coconut Water and Meat inside.  The Meat can be pressed into Coconut Milk, however this is usually only useful for fancier recipes.</p>
<p><b>Game</b></p>
<p>There are often many good animals to hunt in the wild, and making hunting tools is relatively easy: Bows from Logs, for example.</p>
<p>Poultry can be prepared with an Orange sauce to make a delicious meal.  If you're fortunate enough to get Steak, make it into a Jerky, or better, add Fire Spice during the drying process for Spicy Jerky.</p>