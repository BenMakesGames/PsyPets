<b><i>All You Wanted to Know About Sammiches</i></b></p>
<p>Sandwiches are relatively easy and inexpensive to make, especially if you hunt for your own ingredients.  Let's take a look at what goes into preparing a basic sandwich:</p>
<ul>
 <li>1 Bread of any kind</li>
 <li>1 Jelly of any kind OR 1 Egg of any kind</li>
</ul>
<p>Of course, if you're using an Egg, that's very straight forward.  If you want to make a Jelly sandwich, it is possible to use store-bought Jelly, or even make your own Jelly.</p>
<p>Jelly is made by mashing together berries.  For example, two Redsberries will produce Redsberry Jelly.  You can even mix different kinds of berries to produce a swirled jelly - something you can't buy in most stores.</p>
<p>You can also make delicious PB&J Triangles!</p>
<ul>
 <li>1 Bread</li>
 <li>1 Jelly</li>
 <li>1 Peanut Butter</li>
</ul>
<p>Peanut Butter can, again, be store-bought, but it's always best to go with home-made ingredients.  Peanut Butter is as easy as smashing together two Peanuts.</p>
