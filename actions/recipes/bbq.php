<p><b>Steak BBQ</b></p>
<p>Steak BBQ is a simple and delicious recipe.  Simply brush BBQ Sauce over Steak, and cook.</p>
<p>For best results, make your own BBQ Sauce:</p>
<ul>
 <li>Brown Sugar</li>
 <li>Fire Spice</li>
 <li>Ginger</li>
</ul>
<p>This is a very versatile sauce which you can use with just about any meat.  Try it out!</p>
<p><b>Salisbury Steak</b></p>
<p>Salisbury Steak is a wonderful, traditional meal.  It's extremely filling, and the ingredients are fairly easy to come by, too.</p>
<ul>
 <li>Steak</li>
 <li>Egg</li>
 <li>Onion</li>
</ul>
<p>If you can, use Speckled Egg instead of regular Eggs - they have a richer taste.</p>
<p><b>Jerkies</b></p>
<p>Plain Jerky is nothing more than dried Steak, and not particularly rewarding.  For a more interesting, Spicy Jerky, add Fire Spice.</p>