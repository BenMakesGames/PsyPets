<p>A deceptively simple recipe.  As long as you keep an eye on things as they cook, it shouldn't be a problem.</p>
<p><b>Ingredients</b><br></p>
<ul>
 <li>Flour</li>
 <li>Sour Cream</li>
 <li>Beef (Ground)</li>
 <li>Onion</li>
 <li>Mushroom</li>
 <li>Butter</li>
 <li>Noodles (Egg Noodles are recommended)</li>
</ul>
<p><b>Instructions</b></p>
<ol>
 <li><p>Saut&eacute;e the Onion and Mushroom in a pan with Butter until the Onion is a light golden brown.  Put the saut&eacute;e in a bowl, but keep the pan on the stove for the next step.</p></li>
 <li>
  <p>Set the heat to a medium-high temperature and add the Beef.  As it browns, add the Flour.  This may also be a good time to start the water boiling to cook the noodles in.  When there's barely any raw Beef visible, turn down the heat and stir in the Sour Cream and then the saut&eacute;e we had earlier set aside.  Stir while simmering - do not allow to boil!</p>
  <p>Once everything is nicely cooked, remove from heat and cover.</p>
 </li>
 <li><p>Cook the Noodles if you have not already done so.  Egg Noodles won't even need 10 minutes to cook in boiling water.  Others may take longer.</p></li>
 <li><p>Drain noodles and mix in the creamy vegetable-beef saut&eacute;e.  Serve hot.</p></li>
</ol>
<p>Makes quite a lot more than you might expect.</p>