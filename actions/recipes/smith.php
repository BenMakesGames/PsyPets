<i><font class="size14">The Smithy's Guide</font></i><br />
<br />
Knowing how to turn ordinary common resources into useful materials is the Smithy's job, and the Crafter's best friend.<br />
<br />
<b>Dyes</b><br />
<br />
Dyes are one of the easier things to get.<br />
<ul>
 <li>Blue Dye can be squeezed out of fresh Blueberries.</li>
 <li>Red Dye can be extracted at a good rate from Beets, Amethyst Rose, and Tomato.</li>
 <li>Yellow Dye can be collected from Onion.</li> 
</ul>
<p>Additionally, two Tea Leaves can be distilled into Yellow and Blue Dye.</p>
