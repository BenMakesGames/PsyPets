Donuts require some amount of preparation, but rewards you with 4 of your favorite kind of donuts.
<br />
<b>Chocolate Donut Recipe</b><br />
1 Flour<br />
1 Milk<br />
1 Egg<br />
1 Sugar<br />
1 Baking Chocolate<br />
<br />
Be sure not to use chocolate drops for this recipe!  They will not melt, and instead remain chunky - not desirable for our donut topping.<br />
<br />
<b>Sprinkled Donut Recipe</b><br />
1 Flour<br />
1 Milk<br />
1 Egg<br />
2 Sugar<br />
<br />
The only difference between this and the Chocolate Donut recipe is that instead of adding chocolate, we're adding sprinkles!  Sprinkles are made purely of sugar, hence the extra sugar called for by this recipe.<br />