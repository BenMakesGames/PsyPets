&quot;Chocolate lovers beware! Very yummy! An easy Bundt cake made with cake mix, instant pudding, sour cream and chocolate chips!&quot;<br />
<br />
Original recipe yield:<br />
1 - 10 inch Bundt pan<br />
<br />
PREP TIME  10 Min <br />
COOK TIME  1 Hr  <br />
READY IN  1 Hr 10 Min <br />
<br />
INGREDIENTS:<br />
<br />
1 (18.25 ounce) package devil's food cake mix <br />
1 (3.9 ounce) package instant chocolate pudding mix <br />
4 eggs <br />
1 cup sour cream <br />
1/2 cup vegetable oil <br />
1/2 cup water <br />
2 cups semisweet chocolate chips <br />
<br />
DIRECTIONS:<br />
<br />
1. Preheat oven to 350 degrees F (175 degrees C). Grease and flour a 10 inch Bundt pan. Have all ingredients at room temperature. <br />
<br />
2. In a large bowl, stir together cake mix and pudding mix. Make a well in the center and pour in eggs, sour cream, oil and water. Beat on low speed until blended. Scrape bowl, and beat 4 minutes on medium speed. Stir in chocolate chips. Pour batter into prepared pan. <br />
<br />
3. Bake in the preheated oven for 50 to 60 minutes, or until a toothpick inserted into the center of the cake comes out clean. Allow to cool.