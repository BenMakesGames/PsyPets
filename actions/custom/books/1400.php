<span style="font-weight:bold;">On the Ruin of Rome</span><br />
<br />
How dost thou lie, O Rome, neath the foot of the Teuton<br />
Slaves are they men, and bent to the will of thy conqueror;<br />
Wither hath gone, great city, the race that gave law to all nations,<br />
Subdu'd the East and the West, and made them bow down to thy consuls,<br />
Knew not defeat, but gave it to all who attack'd thee?<br />
<br />
Dead! and replac'd by these wretches who cower in confusion.<br />
Dead! they who gave us this empire to guard and to live in,<br />
Rome, thou didst fall from thy pow'r with the proud race that made thee,<br />
and we, base Italians, enjoy'd what we could not have builded.<br />
