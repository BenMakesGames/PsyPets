<span style="font-weight:bold;">To the Old Pagan Religion</span><br />
<br />
Olympian gods! how can I let ye go,<br />
 And pin my faith to this new Christian creed?<br />
Can I resign the deities I know,<br />
 for him who on a cross for man did bleed?<br />
<br />
How in my weakness can my hopes depend<br />
 On one lone god, tho' mighty be his pow'r?<br />
Why can Jove's host no more assistance lend,<br />
 To Soothe my pain, and cheer my troubled hour?<br />
<br />
Are there no dryads on these wooded mounts<br />
 O'er which I oft in desolation roam?<br />
Are there no naiads in these crystal founts<br />
 Or nereids upon the ocean foam?<br />
<br />
Fast spreads the new; the older faith declines;<br />
 The name of Christ resounds upon the air;<br />
But my wrack'd soul in solitude repines<br />
 And gives the gods their last-received pray'r.<br />
