<span style="font-weight:bold;">Ode to Selene or Diana</span><br />
<br />
Immortal Moon, in maiden splendour shine;<br />
 Dispense thy beams, divine Latona's child.<br />
Thy silver rays all grosser things define,<br />
 And hide harsh Truth in sweet illusion mild.<br />
<br />
In thy soft light, the city of unrest<br />
 That stands so squalid in thy brother's glare,<br />
Throws off its habit, and in silence blest,<br />
 Becomes a vision, sparkling bright and fair.<br />
<br />
The modern world, with all its care and pain<br />
 The smokey streets, the loathsome clanging mills,<br />
Face 'neath thy breams, Selene, and again<br />
 We dream as shepherds on Chaldea's hills.<br />
<br />
Take heed, Diana, of my humble plea;<br />
 Convey me where my happiness can last,<br />
Draw me against the tide of Time's rought sea,<br />
 And let my spirit rest amidst the past.<br />
