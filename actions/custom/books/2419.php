Her main pastime appears to be procrastination. Coming up with new and creative ways to do nothing at all takes a surprising amount of time and effort. Other favorites include gaming, drawing, video games and reading. She seems to have an unhealthy obsession with fantasy novels, as can be seen in the fact that nearly all of her pets are named after Wheel of Time characters.<br />
Frustrated at her lack of social skills, Feather once started an organization known as the "Society of Socially Awkward Individuals" Unfortunately, the few people who showed up sat on opposite sides of the meeting room and stared at one another for two hours without speaking. The SSAI was universally regarded as a failure, and Feather has not been seen often in public places since.<br />
A few years ago she underwent a dangerous revolutionary medical procedure that replaced her blood with coffee. All went well, and now Feather lives a happy healthy life as long as she maintains a regular intake of caffeine.<br />
