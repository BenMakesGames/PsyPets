Once upon a time there was a young noob; lets call him George. He wandered in on a dark rainy day, just excited to have found a new game to play. Into the forums he did venture, seeking new friends and maybe a mentor.<br />
"Hmm, Chit-Chat," thought he, "sounds fun! Lets go see." Sex threads, pictures of boobies and oh, the cursing! His poor young eyes, they could not handle it all. Far away from the forums he did flee.<br />
Still, he was determined to stay, although from the forums he kept far away. But Psy was like nothing he'd seen before. Poor George got lost; he knew not what to do. His pets, they did whine, whimper and starve. Gathering his nerve, the young noob decided to brave the forum once more.<br />
"Plz hlp!" was his post. "My pet is no gud. I hav no munny and no food. How do I get sum? Can u giv me nything?" And soon the replies came; but oh! There was no help- only flames.<br />
"Learn some English and read the stickies!"<br />
"Noob! You better read the wiki!"<br />
Here our tale of poor young George comes to an end. He couldn't take the heat, his pets had nothing to eat, and earning money was too great of a feat. The little noob was never seen again.<br />
Twelve months later- to the day!- a box appeared in auction. Many users coveted and bid, but only one user could win. 40,000 m did she pay. Thrilled, she opened to look inside; but to her dismay, only a pamphlet, a clover and a single fluff did she find.<br />
